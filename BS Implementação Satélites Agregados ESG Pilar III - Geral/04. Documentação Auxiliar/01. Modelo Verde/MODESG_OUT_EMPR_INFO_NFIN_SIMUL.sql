        ON CT84.CPAIS_RESIDENCIA = TAT.TAYD91C0_CELEMTAB
    ) CT84
    LEFT JOIN
    -- 2ş CRUZAMENTO: OBTER INFORMAÇĂO SOBRE ZIP_CODE
    (
        SELECT DISTINCT CLYR7003_ZCLIENTE, CLYR7003_CPOSTAL, CLYR7003_CENDREGI
