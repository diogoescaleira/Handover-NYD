        WHERE DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CD_ESTRUTURAIS.TAT91_TABELAS WHERE DATA_DATE_PART <= '${REF_DATE}') AND TAYD91C0_CTABELA='J48'
    ) TAT2
