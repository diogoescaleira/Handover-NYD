            WHEN PR_CP.LOCALIZACAO IS NOT NULL THEN 'Corporaçăo - ZipCode'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL THEN 'Corporaçăo - Nuts'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL THEN 'Corporaçăo - Proxy'
        ELSE 'MISS' END AS ORIGEM_FLG_RSC_FSC_CRNC_SIMUL  
        
        
