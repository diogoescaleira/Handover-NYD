        WHERE DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CD_ESTRUTURAIS.INTERFACE_MORADAS WHERE DATA_DATE_PART <= '${REF_DATE}') AND CLYR7003_IMORFISC ='S'
    ) IF_MORADAS
    ON CAST(CAST(CT84.ZCLIENTE AS BIGINT) AS STRING)  = CAST(CLYR7003_ZCLIENTE AS STRING)
    LEFT JOIN
    -- 3ş CRUZAMENTO: OBTER INFORMAÇĂO DE CODE_NACE_ESG
    (
        SELECT DISTINCT ZCLIENTE, 
                        SPLIT_PART(NACE_ESG_AUX, ".", 1) AS NACE_ESG_AUX
        FROM
        (
            SELECT DISTINCT ZCLIENTE, COD_NACE_ESG AS NACE_ESG_AUX
