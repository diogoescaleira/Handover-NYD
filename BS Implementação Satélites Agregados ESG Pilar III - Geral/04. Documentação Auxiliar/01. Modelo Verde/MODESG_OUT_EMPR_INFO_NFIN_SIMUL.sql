        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL AND PR_CP.FLG_RSC_FSC_AGD = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL AND NUT.FLG_RSC_FSC_AGD = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL AND PROXY.FLG_RSC_FSC_AGD = 'Yes' THEN 1 
        ELSE 0 END AS FLG_RSC_FSC_AGD_SIMUL
        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL AND PR_CP.FLG_RSC_FSC_CRNC = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL AND NUT.FLG_RSC_FSC_CRNC = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL AND PROXY.FLG_RSC_FSC_CRNC = 'Yes' THEN 1
        ELSE 0 END AS FLG_RSC_FSC_CRNC_SIMUL
        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL THEN 'Corporaçăo - ZipCode'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL THEN 'Corporaçăo - Nuts'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL THEN 'Corporaçăo - Proxy'
        ELSE 'MISS' END AS ORIGEM_FLG_RSC_FSC_AGD_SIMUL
