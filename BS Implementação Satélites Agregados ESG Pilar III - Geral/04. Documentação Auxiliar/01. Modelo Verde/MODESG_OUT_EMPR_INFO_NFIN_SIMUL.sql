SELECT X.CONTRAPARTE, X.ORIGEM_SCP1_EMSS_FINAL,Y.origem_scp1_emss,count(*)
FROM 
    (
    SELECT F1.*
        ,CASE WHEN F2.ZCLIENTE IS NOT NULL THEN F2.SCP1_EMSS ELSE F3.SCP1_EMSS END AS SCP1_EMSS_FINAL
