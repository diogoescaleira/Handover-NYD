(
    SELECT  CT84.*
           ,CONCAT('PRT_',CLYR7003_CPOSTAL) AS CPOSTAL
           ,TAYD91C0_NELEMC09 AS CPAIS
           ,NACE_ESG_AUX
           ,TAYD91C0_NELEMC05 AS NUTS
    FROM 
    -- 1ş CRUZAMENTO: OBTER INFORMAÇĂO SOBRE O COD_PAIS APÓS TRADUÇĂO
    (
        SELECT DISTINCT ZCLIENTE, TAYD91C0_NELEMC09
        FROM
        (
            SELECT *
            FROM CD_CAPTOOLS.CT084_UNIV_CLI_D
            WHERE REF_DATE IN (SELECT MAX (REF_DATE) FROM CD_CAPTOOLS.CT084_UNIV_CLI_D WHERE REF_DATE <= '${REF_DATE}') 
        ) CT84
        LEFT JOIN
        (
            SELECT *
            FROM CD_ESTRUTURAIS.TAT91_TABELAS
            WHERE DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CD_ESTRUTURAIS.TAT91_TABELAS WHERE DATA_DATE_PART <= '${REF_DATE}') AND TAYD91C0_CTABELA='015'
