DROP TABLE BU_ESG_WORK.ANL_RISCO_FISICO_COL_JAN25;
CREATE TABLE BU_ESG_WORK.ANL_RISCO_FISICO_COL_JAN25 AS 
SELECT DISTINCT
   BASE.*
        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL AND PR_CP.FLG_RSC_FSC_AGD = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL AND NUT.FLG_RSC_FSC_AGD = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL AND PROXY.FLG_RSC_FSC_AGD = 'Yes' THEN 1 
        ELSE 0 END AS FLG_RSC_FSC_AGD_SIMUL
        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL AND PR_CP.FLG_RSC_FSC_CRNC = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL AND NUT.FLG_RSC_FSC_CRNC = 'Yes' THEN 1
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL AND PROXY.FLG_RSC_FSC_CRNC = 'Yes' THEN 1
        ELSE 0 END AS FLG_RSC_FSC_CRNC_SIMUL
        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL THEN 'CorporańŃo - ZipCode'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL THEN 'CorporańŃo - Nuts'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL THEN 'CorporańŃo - Proxy'
        ELSE '' END AS ORIGEM_FLG_RSC_FSC_AGD_SIMUL
        ,CASE 
            WHEN PR_CP.LOCALIZACAO IS NOT NULL THEN 'CorporańŃo - ZipCode'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NOT NULL THEN 'CorporańŃo - Nuts'
            WHEN PR_CP.LOCALIZACAO IS NULL AND NUT.COD_NUTIII IS NULL AND PROXY.COD_PAIS IS NOT NULL THEN 'CorporańŃo - Proxy'
        ELSE '' END AS ORIGEM_FLG_RSC_FSC_CRNC_SIMUL   
        
        
FROM
(
    SELECT  BASE.*
           ,CONCAT('PRT_',SUBSTR(BASE.POSTAL_CODE,1,4)) AS CPOSTAL
           ,TAYD91C0_NELEMC05 AS NUTS
    FROM 
    -- CRUZAMENTO: OBTER O UNIVERSO BASE 
    --> DEZ24 274.743 REGISTOS 
    (
        SELECT *, CONCAT(PROPERTY_BANK_ID,PROPERTY_BRANCH_CODE,PROPERTY_CONTRACT_ID,PROPERTY_REFERENCE_CODE) AS CHAVE_BEM
        FROM BU_ESG_WORK.PROPERTY
        WHERE DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM BU_ESG_WORK.PROPERTY WHERE DATA_DATE_PART <= '${REF_DATE}')
    )BASE 
    
    -- CRUZAMENTO: OBTER INFORMAŪ├O SOBRE NUTS
    LEFT JOIN
    (
        SELECT DISTINCT TAYD91C0_CELEMTAB,TAYD91C0_NELEMC05
        FROM CD_ESTRUTURAIS.TAT91_TABELAS
        WHERE DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CD_ESTRUTURAIS.TAT91_TABELAS WHERE DATA_DATE_PART <= '${REF_DATE}') AND TAYD91C0_CTABELA='J48'
    ) TAT2
    ON BASE.district_municip_parish_code = TAT2.TAYD91C0_CELEMTAB
) BASE
LEFT JOIN
-- A) CRUZAMENTO: OBTER MARCAŪ├O DE RISCO F═SICO POR ZIP_CODE
(
    SELECT *
    FROM BU_ESG_WORK.MODESG_INP_RISCO_FISICO_ZIPCODE
    WHERE REF_DATE='2025-01-31' AND COD_PAIS='PT' AND cod_nace_esg='Secured'
) PR_CP
ON BASE.CPOSTAL = PR_CP.LOCALIZACAO
    
LEFT JOIN
-- B) CRUZAMENTO: OBTER MARCAŪ├O DE RISCO F═SICO POR NUT
(
    SELECT *
    FROM BU_ESG_WORK.MODESG_INP_RISCO_FISICO
    WHERE REF_DATE='2025-01-31' AND COD_PAIS='PT' AND cod_nace_esg='Secured'
) NUT
ON BASE.NUTS = NUT.COD_NUTIII
    
LEFT JOIN
-- C) CRUZAMENTO: OBTER MARCAŪ├O DE RISCO F═SICO POR PROXY
(
    SELECT *
    FROM BU_ESG_WORK.MODESG_INP_RISCO_FISICO
    WHERE REF_DATE='2025-01-31' AND COD_PAIS='PT' AND cod_nace_esg='Secured' AND TRIM(COD_NUTIII) = ''
) PROXY
ON PROXY.COD_PAIS = 'PT'
