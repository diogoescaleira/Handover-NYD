DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.VEICU_LEVENSH_AUX;
CREATE TABLE BU_CAPTOOLS_WORK.VEICU_LEVENSH_AUX AS

SELECT
	*
	,RANK() OVER (PARTITION BY CHAVE_BEM_LEVENSH ORDER BY MODELO_FINAL_TRATADO DESC) AS RANK_LEVENSH
FROM
	(
	SELECT 
		*
		,LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)
		,(1- (LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)/LENGTH(AUX.MODELO_AUX2_1B)))*100 AS MODELO_TRATADO_CORP
		,(1- (LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)/LENGTH(UNI.MODELO_LEVENSH)))*100 AS MODELO_TRATADO_GT013
		,CASE 
			WHEN (1- (LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)/LENGTH(AUX.MODELO_AUX2_1B)))*100 > (1-(LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)/LENGTH(UNI.MODELO_LEVENSH)))*100 THEN (1- (LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)/LENGTH(AUX.MODELO_AUX2_1B)))*100
			ELSE (1- (LEVENSHTEIN (UNI.MODELO_LEVENSH,AUX.MODELO_AUX2_1B)/LENGTH(UNI.MODELO_LEVENSH)))*100 
		END AS MODELO_FINAL_TRATADO
		FROM
			(
			SELECT 
				CONCAT(CEMPBEM,CKBALBEM,CKCTABEM,CKREFBEM) AS CHAVE_BEM_LEVENSH
				,REGEXP_REPLACE(TRIM(UPPER(MARCA)),'[^a-zA-Z0-9]','') AS MARCA_LEVENSH
				,REGEXP_REPLACE(TRIM(UPPER(MODELO)),'[^a-zA-Z0-9]','') AS MODELO_LEVENSH
				,TRIM(UPPER(CASE
					WHEN TRIM(TIPO_COMBUST) ='2' THEN 'DIESEL'
					WHEN TRIM(TIPO_COMBUST) ='7' THEN 'DIESEL/ELECTRIC'
					WHEN TRIM(TIPO_COMBUST) ='5' THEN 'DIESEL'
					WHEN TRIM(TIPO_COMBUST) ='4' THEN 'ELECTRIC'
					WHEN TRIM(TIPO_COMBUST) ='3' THEN 'LPG'
					WHEN TRIM(TIPO_COMBUST) ='1' THEN 'PETROL'
					WHEN TRIM(TIPO_COMBUST) ='6' THEN 'PETROL/ELECTRIC'
					WHEN TRIM(TIPO_COMBUST) ='' THEN 'Average'
				END)) AS TIPO_MOTOR_LEVENSH
				,TRIM(UPPER(CASE
						WHEN TRIM(CGARANT_BEM) ='7101' THEN 'Passenger car'
						WHEN TRIM(CGARANT_BEM) ='7102' THEN 'Van'
						WHEN TRIM(CGARANT_BEM) ='7103' THEN 'Heavy Vehicle'
						WHEN TRIM(CGARANT_BEM) ='7104' THEN 'Heavy Vehicle'
						WHEN TRIM(CGARANT_BEM) ='7105' THEN 'Passenger car'
						WHEN TRIM(CGARANT_BEM) ='7100' THEN 'NOT APPLICABLE'
						WHEN TRIM(CGARANT_BEM) ='7106' THEN 'Heavy Vehicle'
						WHEN TRIM(CGARANT_BEM) ='7107' THEN 'Heavy Vehicle'
						WHEN TRIM(CGARANT_BEM) ='7108' THEN 'NOT APPLICABLE'
						WHEN TRIM(CGARANT_BEM) ='7109' THEN 'NOT APPLICABLE'
						WHEN TRIM(CGARANT_BEM) ='7110' THEN 'Passenger car'
						WHEN TRIM(CGARANT_BEM) ='7111' THEN 'NOT APPLICABLE'
				END)) AS TIPO_VEICULO_LEVENSH
			FROM cd_garantias.GT013_BENS_MOB
			WHERE REF_DATE='${ref_date}'
				AND CGARANT_BEM LIKE '71%'
			)UNI	
		LEFT JOIN 
			(
			SELECT 
				*
			FROM    
				(
				SELECT 
					SCP1_FTR_EMSS 
					,SCP2_FTR_EMSS 
					,SCP3_FTR_EMSS 
					,CAST(SCP1_FTR_EMSS AS DECIMAL(32,10)) AS SCP1_FTR_EMSS_1B
					,CAST(SCP2_FTR_EMSS AS DECIMAL(32,10)) AS SCP2_FTR_EMSS_1B
					,TRIM(UPPER(MODELO)) AS MODELO_AUX_1B
					,TRIM(UPPER(MARCA)) AS MARCA_AUX_1B
					,REGEXP_REPLACE(TRIM(UPPER(MARCA)),'[^a-zA-Z0-9]','') AS MARCA_AUX2_1B --HF: É necessário adicionar o tratamento do ' ' na especificaçăo 
					,REGEXP_REPLACE(TRIM(UPPER(MODELO)),'[^a-zA-Z0-9]','') AS MODELO_AUX2_1B
					,TRIM(UPPER(TIPO_VEICULO)) AS TIPO_VEICULO_AUX_1B
					,TRIM(UPPER(TIPO_MOTOR))AS TIPO_MOTOR_AUX_1B
					,RANK () OVER (PARTITION BY CONCAT(REGEXP_REPLACE(TRIM(UPPER(MARCA)),'[^a-zA-Z0-9]',''),REGEXP_REPLACE(TRIM(UPPER(MODELO)),'[^a-zA-Z0-9]',''),TRIM(UPPER(TIPO_VEICULO)),TRIM(UPPER(TIPO_MOTOR))) ORDER BY (NVL(CAST(SCP1_FTR_EMSS AS DECIMAL(32,10)),0)+NVL(CAST(SCP2_FTR_EMSS AS DECIMAL(32,10)),0))/2 DESC) AS RANK_1
				FROM BU_ESG_WORK.MODESG_INP_EMSS_VCLS
				WHERE REF_DATE='${ref_date}'
					AND TRIM(UPPER(NOME_PAIS))='PORTUGAL'
					AND TRIM(UPPER(TIPO_MOTOR))<>'AVERAGE'
				)TRAT WHERE RANK_1=1 --HF 03/12 : Tratamento e-mail SB --> BS | Testes ESG Pilar III - Tabela Veículos - Proposta de escolha de 1 registo do input de veículos  
			)AUX ON AUX.MARCA_AUX2_1B = UNI.MARCA_LEVENSH AND AUX.TIPO_VEICULO_AUX_1B = UNI.TIPO_VEICULO_LEVENSH AND AUX.TIPO_MOTOR_AUX_1B = UNI.TIPO_MOTOR_LEVENSH
	)AUX_TRAT

