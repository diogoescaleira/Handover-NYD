DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.EMI_FINAN_AUX2;
CREATE TABLE BU_CAPTOOLS_WORK.EMI_FINAN_AUX2 AS

SELECT F1.*
    ,CASE 
        WHEN F2.CHAVE_CTO IS NULL THEN 0
        ELSE F2.N
    END AS NUM_BENS
    ,F3.CTO_MAVALIAA
    ,CASE
        WHEN F2.CHAVE_CTO IS NULL THEN 'SEM_COLATERAL'
		WHEN F1.SUM_MSALDO_FINAL_CT>0 THEN 'REVER'
		WHEN F3.CTO_MAVALIAA>=-F1.SUM_MSALDO_FINAL_CT AND F3.CTO_MAVALIAA IS NOT NULL AND F1.SUM_MSALDO_FINAL_CT IS NOT NULL THEN 'SUM_COLAT_COBRE'
        WHEN F3.CTO_MAVALIAA<-F1.SUM_MSALDO_FINAL_CT AND F3.CTO_MAVALIAA IS NOT NULL AND F1.SUM_MSALDO_FINAL_CT IS NOT NULL THEN 'SUM_COLAT_N_COBRE'
