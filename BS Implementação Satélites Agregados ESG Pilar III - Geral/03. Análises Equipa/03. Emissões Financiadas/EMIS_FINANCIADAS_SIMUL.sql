	,L1.SCP1_EMSS AS SCP1_EMSS_CLI,L1.SCP2_EMSS AS SCP2_EMSS_CLI,L1.SCP3_EMSS AS SCP3_EMSS_CLI
	,L1.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_CLI,L1.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_CLI,L1.ORIGEM_SCP3_EMSS AS ORIGEM_SCP3_EMSS_CLI
	,L1.DIVIDA_TOTAL,L1.CAPITAL_PROPRIO
	,L2.SCP1_EMSS AS SCP1_EMSS_MOB,L2.SCP2_EMSS AS SCP2_EMSS_MOB,L2.SCP3_EMSS AS SCP3_EMSS_MOB
	,L2.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_MOB,L2.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_MOB,L2.ORIGEM_SCP3_EMSS AS ORIGEM_SCP3_EMSS_MOB
	,L3.SCP1_EMSS AS SCP1_EMSS_IMO,L3.SCP2_EMSS AS SCP2_EMSS_IMO,L3.SCP3_EMSS AS SCP3_EMSS_IMO
	,L3.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_IMO,L3.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_IMO,L3.ORIGEM_SCP3_EMSS AS ORIGEM_SCP3_EMSS_IMO
	,L5.preco_aquisicao
FROM 
    (
	SELECT *
	FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
	)UNI
LEFT JOIN 
	(
	SELECT ZCLIENTE,SCP1_EMSS,SCP2_EMSS,SCP3_EMSS,ORIGEM_SCP1_EMSS,ORIGEM_SCP2_EMSS,ORIGEM_SCP3_EMSS,DIVIDA_TOTAL,CAPITAL_PROPRIO
	FROM bu_esg_work.modesg_out_empr_info_nfin
	WHERE ref_date='${ref_date}'
	)L1 ON UNI.ZCLIENTE=L1.ZCLIENTE
LEFT JOIN 
	(
	SELECT CONCAT(CEMPBEM,CKBALBEM,ckctabem,ckrefbem) AS CHAVE_MOB,SCP1_EMSS,SCP2_EMSS,SCP3_EMSS,ORIGEM_SCP1_EMSS,ORIGEM_SCP2_EMSS,ORIGEM_SCP3_EMSS
	FROM BU_ESG_WORK.modesg_out_emss_vcls 
	WHERE ref_date='${ref_date}'
	)L2 ON UNI.CHAVE_BEM=L2.CHAVE_MOB
LEFT JOIN 
	(
	SELECT CONCAT(CEMPBEM,CKBALBEM,ckctabem,ckrefbem) AS CHAVE_IMO,SCP1_EMSS,SCP2_EMSS,SCP3_EMSS,ORIGEM_SCP1_EMSS,ORIGEM_SCP2_EMSS,ORIGEM_SCP3_EMSS
	FROM BU_ESG_WORK.modesg_out_bens_imoveis 
	WHERE ref_date='${ref_date}'
	)L3 ON UNI.CHAVE_BEM=L3.CHAVE_IMO
LEFT JOIN 	
	(
	SELECT DISTINCT CONCAT(CEMPBEM,CKBALBEM,ckctabem,ckrefbem) AS CHAVE_MUG,preco_aquisicao
	FROM cd_garantias.gt018_rateio_aval
	WHERE ref_date='${ref_date}'
		and preco_aquisicao is not null 
	)L5 ON UNI.CHAVE_BEM=L5.CHAVE_MUG	
;
