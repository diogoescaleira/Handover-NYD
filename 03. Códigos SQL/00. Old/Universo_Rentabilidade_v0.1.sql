        SUM(C299) AS C299, 
        SUM(C300) AS C300, 
        SUM(M009) AS M009, 
        SUM(AC310) AS AC310
        
    FROM CD_CAPTOOLS.CT209_RENT_KPM
    WHERE REF_DATE = '${DATE}'
    AND ORIGEM NOT IN ('CAPITAL', 'CONTABILIDADE')
    AND C325=0
    AND C453=0
    GROUP BY 1,2,3,4,5,6,7,8,9
