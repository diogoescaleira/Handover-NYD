	AND AMBITO = 'CDG'
	AND UPPER(TIPO_CONTA_LOCAL) NOT LIKE "%PASSIVO%"
	AND TRIM(METAPRODUTO) <> ''
    ) CT831
    
ON CT209.CKMETAMIS_CT209 <=> CT831.METAPRODUTO
-- )A
