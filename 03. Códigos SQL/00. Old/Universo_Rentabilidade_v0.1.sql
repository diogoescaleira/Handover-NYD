CREATE TABLE BU_CAPTOOLS_WORK.UNIV_RENTAB_JAN25_2 AS

--  SELECT COUNT (DISTINCT CEMPCTA, CKBALCAO, CKNUMCTA,ZDEPOSIT) FROM (
select distinct * 
from 
    (
    SELECT  
        CEMPCTA as CEMPCTA_CT211, 
        CKBALCAO AS CKBALCAO_CT211, 
        CKNUMCTA AS CKNUMCTA_CT211, 
        ZDEPOSIT AS ZDEPOSIT_CT211,
        CKPRODMI AS CKPRODMI_CT211,
        ZSEQDIV AS ZSEQDIV_CT211,
        CHCRUZA AS CHCRUZA_CT211,
        CMETACOM AS CMETACOM_CT211,
        ZCLIENTE AS ZCLIENTE_CT211,
        SUM(C457) AS C457,
        SUM(C458) AS C458,
        SUM(C459) AS C459,
        SUM(C460) AS C460
    
    FROM CD_CAPTOOLS.CT211_RENT_KPMCO
    WHERE REF_DATE = '${DATE}'
    AND ORIGEM NOT IN ('CAPITAL', 'CONTABILIDADE')
    AND (ABS(C457)+ABS(C458)+ABS(C459)+ABS(C460)) <> 0 
    GROUP BY 1,2,3,4,5,6,7,8,9
    ) CT211
inner join 
    (
	SELECT DISTINCT METACOMISSAO, TIPO_CONTA_LOCAL, AMBITO
	FROM CD_CAPTOOLS.CT831_CLASS_META
	WHERE REF_DATE = '${DATE}'
	AND AMBITO = 'CDG'
	AND UPPER(TIPO_CONTA_LOCAL) NOT LIKE "%PASSIVO%"
	AND TRIM(METACOMISSAO) <> ''
    ) CT831

on CT831.METACOMISSAO = CT211.CMETACOM_CT211
-- ) A
;

--Passo 3: Full join entre passo 1 e passo 2
-- # Registos: 4 106 960 

DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.UNIV_RENTAB_JAN25_3;
CREATE TABLE BU_CAPTOOLS_WORK.UNIV_RENTAB_JAN25_3 AS

SELECT DISTINCT
    COALESCE(CEMPCTA_CT209,CEMPCTA_CT211) AS CEMPCTA,
    COALESCE(CKBALCAO_CT209,CKBALCAO_CT211) AS CKBALCAO, 
    COALESCE(CKNUMCTA_CT209, CKNUMCTA_CT211) AS CKNUMCTA, 
    COALESCE(ZDEPOSIT_CT209,ZDEPOSIT_CT211) AS ZDEPOSIT, 
    COALESCE(ZSEQDIV_CT209,ZSEQDIV_CT211) AS ZSEQDIV, 
    COALESCE(ZCLIENTE_CT209, ZCLIENTE_CT211) AS ZCLIENTE,
    COALESCE(CKPRODMI_CT209,CKPRODMI_CT211) AS CKPRODMI, 
    COALESCE(CHCRUZA_CT209,CHCRUZA_CT211) AS CHCRUZA,
    -- CAMPOS CT209 
    C001,
    C007,
    C008,
    C011,
    C134,
    C224,
    C295,
    C298,
    C299,
    C300,
    M009,
    AC310,
    METAPRODUTO,
    -- CAMPOS CT211
    C457,
    C458,
    C459,
    C460,
    METACOMISSAO,
    TIPO_CONTA_LOCAL
FROM BU_CAPTOOLS_WORK.UNIV_RENTAB_JAN25_1 A
    
    FULL JOIN BU_CAPTOOLS_WORK.UNIV_RENTAB_JAN25_2 B 
    
    ON A.CEMPCTA_CT209=B.CEMPCTA_CT211
    AND A.CKBALCAO_CT209=B.CKBALCAO_CT211
    AND A.CKNUMCTA_CT209=B.CKNUMCTA_CT211
    AND A.ZDEPOSIT_CT209=B.ZDEPOSIT_CT211
    AND A.ZSEQDIV_CT209=B.ZSEQDIV_CT211 -- 
    AND A.ZCLIENTE_CT209=B.ZCLIENTE_CT211
    AND A.CKPRODMI_CT209=B.CKPRODMI_CT211
    AND A.CHCRUZA_CT209=B.CHCRUZA_CT211 -- 
    ) X
;

