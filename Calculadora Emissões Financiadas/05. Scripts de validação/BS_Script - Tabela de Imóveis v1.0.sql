	SELECT *
	FROM
		(
		SELECT *
			,TRIM(UPPER(EPC)) AS EPC_AUX
			,TRIM(UPPER(TIPO_FINALIDADE_BEM)) AS TIPO_FINALIDADE_BEM_AUX
			,ROW_NUMBER() OVER (PARTITION BY EPC,TIPO_FINALIDADE_BEM ORDER BY ANO_FATOR_EMISSAO DESC) AS ROW_1
		FROM BUSINESS_ESG.modesg_inp_emss_bens_imoveis
		WHERE REF_DATE='${ref_date}'
			AND TRIM(UPPER(NOME_PAIS))='PORTUGAL'
			AND TRIM(UPPER(NOME_UNIDADE_FUNCIONAL))='FLOOR AREA'
			AND (TRIM(EPC)='' OR EPC IS NULL)
		)AUX WHERE ROW_1=1
	)EMISS_1B ON EPC.CHAVE_BEM IS NOT NULL AND BASE.NET_FLOOR_AREA<>0 AND EMISS_1B.TIPO_FINALIDADE_BEM_AUX=FIN_BEM.TIPO_FINALIDADE_BEM

LEFT JOIN 
	-- 6ş Cruzamento: Obter as métricas para o universo sem EPC mas com Área útil
	--> SET24 275.266 Registos
	(
	SELECT *
	FROM
		(
		SELECT *
			,TRIM(UPPER(EPC)) AS EPC_AUX
			,TRIM(UPPER(TIPO_FINALIDADE_BEM)) AS TIPO_FINALIDADE_BEM_AUX
			,ROW_NUMBER() OVER (PARTITION BY EPC,TIPO_FINALIDADE_BEM ORDER BY ANO_FATOR_EMISSAO DESC) AS ROW_1
		FROM BUSINESS_ESG.modesg_inp_emss_bens_imoveis
		WHERE REF_DATE='${ref_date}'
			AND TRIM(UPPER(NOME_PAIS))='PORTUGAL'
			AND TRIM(UPPER(NOME_UNIDADE_FUNCIONAL))='FLOOR AREA'
			AND (TRIM(EPC)='' OR EPC IS NULL)
		)AUX WHERE ROW_1=1
	)EMISS_2 ON EPC.CHAVE_BEM IS NULL AND BASE.NET_FLOOR_AREA<>0 AND EMISS_2.TIPO_FINALIDADE_BEM_AUX=FIN_BEM.TIPO_FINALIDADE_BEM

LEFT JOIN 
	-- 7ş Cruzamento: Obter as métricas para o universo sem Área útil 
	--> SET24 275.266 Registos
	(
	SELECT *
	FROM
		(
		SELECT *
			,TRIM(UPPER(EPC)) AS EPC_AUX
			,TRIM(UPPER(TIPO_FINALIDADE_BEM)) AS TIPO_FINALIDADE_BEM_AUX
			,ROW_NUMBER() OVER (PARTITION BY EPC,TIPO_FINALIDADE_BEM ORDER BY ANO_FATOR_EMISSAO DESC) AS ROW_1
		FROM BUSINESS_ESG.modesg_inp_emss_bens_imoveis
		WHERE REF_DATE='${ref_date}'
			AND TRIM(UPPER(NOME_PAIS))='PORTUGAL'
			AND TRIM(UPPER(NOME_UNIDADE_FUNCIONAL))='DWELLING'
			AND (TRIM(EPC)='' OR EPC IS NULL)
		)AUX WHERE ROW_1=1
	)EMISS_3 ON BASE.NET_FLOOR_AREA=0  AND EMISS_3.TIPO_FINALIDADE_BEM_AUX=FIN_BEM.TIPO_FINALIDADE_BEM

LEFT JOIN 
	-- 8ş Cruzamento: Obter as métricas para o universo sem Área útil que năo foi possivel obter no 7ş cruzamento (nestes casos é assumido a seguinte finalidade: NON-RESIDENTIAL BUILDINGS)
	--> SET24 275.266 Registos
	(
	SELECT *
	FROM
		(
		SELECT *
			,TRIM(UPPER(EPC)) AS EPC_AUX
			,TRIM(UPPER(TIPO_FINALIDADE_BEM)) AS TIPO_FINALIDADE_BEM_AUX
			,ROW_NUMBER() OVER (PARTITION BY EPC,TIPO_FINALIDADE_BEM ORDER BY ANO_FATOR_EMISSAO DESC) AS ROW_1
		FROM BUSINESS_ESG.modesg_inp_emss_bens_imoveis
		WHERE REF_DATE='${ref_date}'
			AND TRIM(UPPER(NOME_PAIS))='PORTUGAL'
			AND TRIM(UPPER(NOME_UNIDADE_FUNCIONAL))='DWELLING'
			AND (TRIM(EPC)='' OR EPC IS NULL)
		)AUX WHERE ROW_1=1
	)EMISS_4 ON BASE.NET_FLOOR_AREA=0  AND EMISS_4.TIPO_FINALIDADE_BEM_AUX=FIN_BEM.TIPO_FINALIDADE_BEM_2

;

