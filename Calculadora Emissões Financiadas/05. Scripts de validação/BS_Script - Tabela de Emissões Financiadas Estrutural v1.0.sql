DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.EMI_FINAN_AUX2 PURGE;
CREATE TABLE BU_CAPTOOLS_WORK.EMI_FINAN_AUX2 AS

SELECT F1.*
    ,CASE 
        WHEN F2.CHAVE_CTO IS NULL THEN 0
        ELSE F2.N
    END AS NUM_BENS
    ,F3.CTO_MAVALIAA
    ,CASE
        WHEN F2.CHAVE_CTO IS NULL THEN 'SEM_COLATERAL'
		WHEN F1.SUM_MSALDO_FINAL_CT>0 THEN 'REVER'
		WHEN F3.CTO_MAVALIAA>=-F1.SUM_MSALDO_FINAL_CT AND F3.CTO_MAVALIAA IS NOT NULL AND F1.SUM_MSALDO_FINAL_CT IS NOT NULL THEN 'SUM_COLAT_COBRE'
        WHEN F3.CTO_MAVALIAA<-F1.SUM_MSALDO_FINAL_CT AND F3.CTO_MAVALIAA IS NOT NULL AND F1.SUM_MSALDO_FINAL_CT IS NOT NULL THEN 'SUM_COLAT_N_COBRE'
        WHEN F1.SUM_MSALDO_FINAL_CT IS NULL THEN 'SALDO_CT_NULL'
		WHEN F1.SUM_MSALDO_FINAL_CT=0 THEN 'SALDO_CT_ZERO'
        WHEN F3.CTO_MAVALIAA IS NULL THEN 'SALDO_COLAT_NULL'
    END AS FLAG_COBERT
    ,ABS(CAST(F1.SUM_MAVALIAA AS DECIMAL(38,20))/CAST(F3.CTO_MAVALIAA AS DECIMAL(38,20))) AS PESO_COLAT_COBERT
	,ABS(CAST(F1.SUM_MAVALIAA AS DECIMAL(38,20))/CAST(F1.SUM_MSALDO_FINAL_CT AS DECIMAL(38,20))) AS PESO_COLAT_N_COBERT
FROM 
    (
    SELECT * 
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX1
    )F1
LEFT JOIN 
    (
    SELECT CHAVE_CTO, COUNT(DISTINCT CHAVE_BEM) AS N
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX1
    WHERE CHAVE_BEM IS NOT NULL 
    GROUP BY 1
    )F2 ON F1.CHAVE_CTO=F2.CHAVE_CTO
LEFT JOIN 
    (
    SELECT CHAVE_CTO,SUM(NVL(SUM_MAVALIAA,0)) AS CTO_MAVALIAA --27/06 - Năo pode existir casos que seja local/corp e em simultaneo que seja local ou corp com bens associados // Após correr esta tabela correr validaçăo #1
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX1
    GROUP BY 1
    )F3 ON F1.CHAVE_CTO=F3.CHAVE_CTO
;

	-- VALIDAÇĂO #1 - VALOR DOS BENS 

SELECT *
FROM 
    (
    SELECT CHAVE,FLAG_PERIMETRO,SUM(SUM_MAVALIAA) AS TESTE
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX2
    GROUP BY 1,2
    )F1
LEFT JOIN 
    (
    SELECT DISTINCT CHAVE,FLAG_PERIMETRO,cto_mavaliaa
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX2
    )F2
ON F1.CHAVE=F2.CHAVE AND F1.FLAG_PERIMETRO=F2.FLAG_PERIMETRO
WHERE (TESTE<>cto_mavaliaa) OR (TESTE IS NOT NULL AND cto_mavaliaa IS NULL) OR (TESTE IS NULL AND cto_mavaliaa IS NOT NULL)

