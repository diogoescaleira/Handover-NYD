SELECT UNI.*
	,L1.SCP1_EMSS AS SCP1_EMSS_CLI,L1.SCP2_EMSS AS SCP2_EMSS_CLI,L1.SCP3_EMSS AS SCP3_EMSS_CLI
	,L1.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_CLI,L1.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_CLI,L1.ORIGEM_SCP3_EMSS AS ORIGEM_SCP3_EMSS_CLI
	,L1.DIVIDA_TOTAL,L1.CAPITAL_PROPRIO
	,L2.SCP1_EMSS AS SCP1_EMSS_MOB,L2.SCP2_EMSS AS SCP2_EMSS_MOB,L2.SCP3_EMSS AS SCP3_EMSS_MOB
	,L2.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_MOB,L2.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_MOB,L2.ORIGEM_SCP3_EMSS AS ORIGEM_SCP3_EMSS_MOB
	,L3.SCP1_EMSS AS SCP1_EMSS_IMO,L3.SCP2_EMSS AS SCP2_EMSS_IMO,L3.SCP3_EMSS AS SCP3_EMSS_IMO
	,L3.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_IMO,L3.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_IMO,L3.ORIGEM_SCP3_EMSS AS ORIGEM_SCP3_EMSS_IMO
	-- Flag para identificar se o bem deve calcular emissăo financiada (ou seja 1) ou năo (ou seja 0) // Se o contrato tiver todos os bens com 0 entăo a emissăo deve ser calculada ao nível do cliente 
	,CASE 
		WHEN TRIM(UNI.PURPOSE_CODE) IN ('Habitacao','CRE_FC','CRE_OUTRO','CRE') AND UNI.FLAG_BEM='MUG_IMO' AND L3.CHAVE_IMO IS NULL THEN 0 
		WHEN TRIM(UNI.PURPOSE_CODE) IN ('Habitacao','CRE_FC','CRE_OUTRO','CRE') AND UNI.FLAG_BEM='MUG_IMO' AND TRIM(L3.ORIGEM_SCP1_EMSS)='Nao Aplicavel' AND TRIM(L3.ORIGEM_SCP2_EMSS)='Nao Aplicavel' AND TRIM(L3.ORIGEM_SCP3_EMSS)='Nao Aplicavel' THEN 0
		WHEN TRIM(UNI.PURPOSE_CODE) IN ('Habitacao','CRE_FC','CRE_OUTRO','CRE') AND UNI.FLAG_BEM='MUG_IMO' THEN 1 
		WHEN TRIM(UNI.PURPOSE_CODE) NOT IN ('Habitacao','CRE_FC','CRE_OUTRO','CRE') AND UNI.FLAG_BEM='MUG_MOV' AND L2.CHAVE_MOB IS NULL THEN 0
		WHEN TRIM(UNI.PURPOSE_CODE) NOT IN ('Habitacao','CRE_FC','CRE_OUTRO','CRE') AND UNI.FLAG_BEM='MUG_MOV' AND TRIM(L2.ORIGEM_SCP1_EMSS)='Nao Aplicavel' AND TRIM(L2.ORIGEM_SCP2_EMSS)='Nao Aplicavel' AND TRIM(L2.ORIGEM_SCP3_EMSS)='Nao Aplicavel' THEN 0
