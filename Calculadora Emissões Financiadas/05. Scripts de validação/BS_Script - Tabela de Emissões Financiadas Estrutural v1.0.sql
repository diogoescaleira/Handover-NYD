			FROM 
				(
				SELECT CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT) AS CHAVE_SALDO
					,CCONTAB_FINAL_ST_SGPS
					,ccontab_final_idcomb --CCONTAB_FINAL_CARGABAL
