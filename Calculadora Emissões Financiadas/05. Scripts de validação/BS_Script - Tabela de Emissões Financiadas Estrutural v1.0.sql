	SELECT DISTINCT CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT,CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT) AS CHAVE,ZCLIENTE
