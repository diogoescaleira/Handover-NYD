DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.EMI_FINAN_AUX1 PURGE;
CREATE TABLE BU_CAPTOOLS_WORK.EMI_FINAN_AUX1 AS

SELECT BASE.*,L6.PURPOSE_CODE,UNIV_CT.FLAG_PERIMETRO,UNIV_CT.SUM_MSALDO_FINAL_CT,BENS.CHAVE_CTO,BENS.CEMPBEM,BENS.CKBALBEM,BENS.CKCTABEM,BENS.CKREFBEM,BENS.CHAVE_BEM,BENS.FLAG_BEM,L5.cgarant_bem,GT018.SUM_mavaliaa,L5.preco_aquis
