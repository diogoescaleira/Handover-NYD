		SELECT CHAVE_SALDO,FLAG_PERIMETRO,SUM(SUM_MSALDO_FINAL_AUX) AS SUM_MSALDO_FINAL_CT
		FROM 
			(
			SELECT *
				,CASE 
					WHEN F2.CONTA_1 IS NOT NULL AND F3.CONTA_2 IS NOT NULL THEN 'ST SGPS Cons;Individual Idcomb'
					WHEN F2.CONTA_1 IS NOT NULL AND F3.CONTA_2 IS NULL THEN 'ST SGPS Cons'
