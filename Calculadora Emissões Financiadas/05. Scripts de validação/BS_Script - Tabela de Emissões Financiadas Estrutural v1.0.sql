DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.EMI_FINAN_AUX3 PURGE;
CREATE TABLE BU_CAPTOOLS_WORK.EMI_FINAN_AUX3 AS

SELECT UNI.*
    ,CASE 
        WHEN FLAG_COBERT='SUM_COLAT_COBRE' THEN PESO_COLAT_COBERT*SUM_MSALDO_FINAL_CT 
        WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' THEN PESO_COLAT_N_COBERT*SUM_MSALDO_FINAL_CT 
        ELSE SUM_MSALDO_FINAL_CT
    END AS AMOUNT
	,F2.*
	,CAST(F2.VCPIM AS DECIMAL(38,20)) AS CLIENT_OWN_FUNDS
	,(CAST(F2.VPNICSFIN AS DECIMAL(38,20)) + CAST(F2.VPNPASLF AS DECIMAL(38,20)) + CAST(F2.VPNOPFIN AS DECIMAL(38,20)) + CAST(F2.VPCICSFIN AS DECIMAL(38,20))+ CAST(F2.VPCOPFIN AS DECIMAL(38,20))+ CAST(F2.VPCPASLF AS DECIMAL(38,20))) AS CLIENT_DEBT
FROM 
    (
        SELECT *,'LINHA INI' AS FLAG_LINHAS FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX2
    UNION ALL 
        SELECT DISTINCT
            F1.CEMPRESA
            ,F1.CBALCAO
            ,F1.CNUMECTA
            ,F1.ZDEPOSIT
            ,F1.CHAVE
            ,F1.ZCLIENTE
			,F1.PURPOSE_CODE
			,F1.FLAG_PERIMETRO
			,F1.SUM_MSALDO_FINAL_CT
			,F1.CHAVE_CTO
			,NULL AS CEMPBEM
			,NULL AS CKBALBEM
			,NULL AS CKCTABEM
			,NULL AS CKREFBEM
            ,NULL AS CHAVE_BEM
            ,NULL AS FLAG_BEM
			,NULL AS cgarant_bem
            ,NULL AS SUM_MAVALIAA
			,NULL AS preco_aquis
			,NULL AS preco_aquisicao
            ,NULL AS NUM_BENS
            ,F1.CTO_MAVALIAA
            ,F1.FLAG_COBERT			
            ,NULL PESO_COLAT_COBERT
            ,CAST(1-F2.PESO_REMN AS DECIMAL(38,20)) AS PESO_COLAT_N_COBERT
            ,'LINHA ADI' AS FLAG_LINHAS  
        FROM 
            (
            SELECT *
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX2
            WHERE FLAG_COBERT='SUM_COLAT_N_COBRE'
            )F1
        LEFT JOIN 
            (
            SELECT CHAVE,SUM(NVL(PESO_COLAT_N_COBERT,0)) AS PESO_REMN
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX2
            WHERE FLAG_COBERT='SUM_COLAT_N_COBRE'
            GROUP BY 1
            )F2 ON F1.CHAVE=F2.CHAVE
	)UNI
LEFT JOIN 
	(
	SELECT ZCLIENTE,CCLI_GRUPO
	FROM CD_CAPTOOLS.CT003_UNIV_CLI
	WHERE REF_DATE = "${ref_date}"
	)F1 ON UNI.ZCLIENTE=F1.ZCLIENTE
LEFT JOIN 
	(
	SELECT *
	FROM 
		(
		SELECT ROW_NUMBER() OVER (PARTITION BY BL.CCLIGRUPO ORDER BY BL.CGESTOR DESC, `TIMESTAMP` DESC) AS ROW_ORDEM_1,BL.*
		FROM 
			(
			SELECT CCLIGRUPO,
				DANO,
				CGESTOR,
				`TIMESTAMP`,
				DFIM,
				CPERIODO,
				ITIP_INF,
				DATA_DATE_PART
				,VCPIM 
				,VPNICSFIN 
				,VPNPASLF 
				,VPNOPFIN 
				,VPCICSFIN
				,VPCOPFIN 
				,VPCPASLF 
			FROM CD_RISCOS.RSV176SA1_BALANCO
			WHERE DATA_DATE_PART <= "${ref_date}"
			)BL
		INNER JOIN 
			(
			SELECT *
			FROM 	
				(
				SELECT ROW_NUMBER() OVER (PARTITION BY CCLIGRUPO ORDER BY DANO DESC, CGESTOR DESC, DFIM DESC) AS ROW_ORDEM_2
					,CCLIGRUPO
					,DANO
					,CGESTOR
					,DFIM
					,ITIP_INF
					,CPERIODO
				FROM 
					(
					SELECT BL1.*
					FROM 
						(
						SELECT 
							CCLIGRUPO
							,DANO
							,CGESTOR
							,DFIM
							,CPERIODO
							,ITIP_INF
						FROM CD_RISCOS.RSV176SA1_BALANCO
						WHERE DATA_DATE_PART <= '${ref_date}'
							AND CPERIODO = '212'
							AND ITIP_INF = 'I'
						)BL1
					INNER JOIN 
						(
						SELECT CCLI_GRUPO,DCONSTITUICAO
						FROM CD_CAPTOOLS.CT003_UNIV_CLI
						WHERE REF_DATE = "${ref_date}"
						)CLI ON CCLI_GRUPO = CCLIGRUPO
					WHERE DANO <= SUBSTR("${ref_date}",1,4) AND DANO >= SUBSTR(DCONSTITUICAO,1,4)
					)AUX_1
				)AUX_2 WHERE ROW_ORDEM_2=1
			)FS ON (FS.CCLIGRUPO = BL.CCLIGRUPO AND FS.DANO = BL.DANO AND FS.CPERIODO = BL.CPERIODO AND FS.ITIP_INF = BL.ITIP_INF)
		)AUX_3 WHERE ROW_ORDEM_1=1
	)F2 ON F1.CCLI_GRUPO=F2.CCLIGRUPO
;

	-- VALIDAÇĂO #2 -SALDOS - CORRER ESTA VALIDAÇĂO APÓS A CRIAÇĂO DESTA TABELA 
SELECT *
FROM 
    (
    SELECT DISTINCT CHAVE,flag_perimetro,SUM_MSALDO_FINAL_CT
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX1
    )F1
LEFT JOIN 
    (
    SELECT CHAVE,flag_perimetro,SUM(AMOUNT) AS AMOUNT_TESTE
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
    GROUP BY 1,2
    )F2 ON F1.CHAVE=F2.CHAVE AND F1.flag_perimetro=F2.flag_perimetro
WHERE ROUND(AMOUNT_TESTE,2)<>ROUND(SUM_MSALDO_FINAL_CT,2) OR (AMOUNT_TESTE IS NULL AND SUM_MSALDO_FINAL_CT IS NOT NULL)  OR (AMOUNT_TESTE IS NOT NULL AND SUM_MSALDO_FINAL_CT IS NULL)
;

	-- VALIDAÇĂO #3 - Linhas adicionadas referentes apenas a bens  
SELECT DISTINCT flag_bem
FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
WHERE CHAVE IN 
    (
    SELECT DISTINCT CHAVE 
    FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
    WHERE FLAG_LINHAS='LINHA ADI'
    )
AND FLAG_LINHAS<>'LINHA ADI'
AND flag_perimetro<>'Nao Aplicavel'
;

