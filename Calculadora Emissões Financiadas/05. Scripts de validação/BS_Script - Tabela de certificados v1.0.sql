	--,(CAST(REGEXP_REPLACE(REGEXP_REPLACE(F1.NET_FLOOR_AREA_OCR,'[^0-9,.]',''),',','.') AS DECIMAL(18,6))/F2.SUM_NET_FLOOR_AREA_OCR)*CAST(REGEXP_REPLACE(REGEXP_REPLACE(F1.CURRENT_ENERGY_RATING_PERCENTAGE_OCR,'[^0-9,.]',''),',','.')  AS DECIMAL(18,6)) AS POND_CURRENT_ENERGY_RATING_PERCENTAGE --21/04 - Sem necessidade de cálculo uma vez que este valor vai ser obtido no certificado (sem desenvolvimento do filtro a aplicar)

FROM
	(
	SELECT 	COLLATERALASSETID_OCR
		,NET_FLOOR_AREA_OCR
		,CARBON_EMISSION_VALUE_YEAR_OCR
		,ENERGY_CONSUMPTION_VALUE_YEAR_OCR
		,PRIMARY_ENERGY_REQ_VALUE_OCR
		,REN_ENERGY_PRODUCED_VALUE_OCR
		,CURRENT_ENERGY_RATING_PERCENTAGE_OCR
	FROM BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_DEZ24 
	)F1 
LEFT JOIN 
	(
	SELECT 
		COLLATERALASSETID_OCR AS SUM_COLLATERALASSETID_OCR 
		,SUM(CAST(REGEXP_REPLACE(REGEXP_REPLACE(NET_FLOOR_AREA_OCR,'[^0-9,.]',''),',','.') AS DECIMAL(18,6))) AS SUM_NET_FLOOR_AREA_OCR
		,SUM(CAST(REGEXP_REPLACE(REGEXP_REPLACE(CARBON_EMISSION_VALUE_YEAR_OCR,'[^0-9,.]',''),',','.')  AS DECIMAL(18,6))) AS SUM_CARBON_EMISSION_VALUE_YEAR_OCR
		,SUM(CAST(REGEXP_REPLACE(REGEXP_REPLACE(ENERGY_CONSUMPTION_VALUE_YEAR_OCR,'[^0-9,.]',''),',','.')  AS DECIMAL(18,6))) AS SUM_ENERGY_CONSUMPTION_VALUE_YEAR_OCR
		,SUM(CAST(REGEXP_REPLACE(REGEXP_REPLACE(PRIMARY_ENERGY_REQ_VALUE_OCR,'[^0-9,.]',''),',','.')  AS DECIMAL(18,6))) AS SUM_PRIMARY_ENERGY_REQ_VALUE_OCR
	FROM BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_DEZ24
	GROUP BY 1
	)F2 ON F1.COLLATERALASSETID_OCR=F2.SUM_COLLATERALASSETID_OCR
