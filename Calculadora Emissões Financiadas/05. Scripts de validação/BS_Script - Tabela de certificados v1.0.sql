DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_STEP2_DEZ24;
CREATE TABLE BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_STEP2_DEZ24 AS

SELECT *
FROM
	(
	SELECT *
		,RANK() OVER (PARTITION BY COLLATERALASSETID_OCR ORDER BY ID_OCR DESC) AS RANK_TRAT_10
	FROM
		(
		SELECT *
			,RANK() OVER (PARTITION BY COLLATERALASSETID_OCR ORDER BY CREATIONDATE_OCR DESC) AS RANK_TRAT_9
		FROM
			(
			SELECT *
				,RANK() OVER (PARTITION BY COLLATERALASSETID_OCR ORDER BY FLAG_CAMPOS_PREENC DESC) AS RANK_TRAT_8
			FROM
				(
				SELECT *
					,RANK() OVER (PARTITION BY COLLATERALASSETID_OCR ORDER BY NVL(CAST(REGEXP_REPLACE(REGEXP_REPLACE(NET_FLOOR_AREA_OCR,'[^0-9,.]',''),',','.') AS DECIMAL(18,6)),0) DESC) AS RANK_TRAT_7
				FROM 
					(
					SELECT *
						,RANK() OVER (PARTITION BY COLLATERALASSETID_OCR ORDER BY FLAG_ORDEM_EPC DESC) AS RANK_TRAT_6
					FROM BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_DEZ24
					)TRATAMENTO_5 WHERE RANK_TRAT_6=1
				)TRATAMENTO_6 WHERE RANK_TRAT_7=1
			)TRATAMENTO_7 WHERE RANK_TRAT_8=1
		)TRATAMENTO_8 WHERE RANK_TRAT_9=1
	)TRATAMENTO_9 WHERE RANK_TRAT_10=1
