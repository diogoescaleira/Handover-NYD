DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.INPUT_GESTEMPR_ADENE_AUX1_DEZ24;
CREATE TABLE BU_CAPTOOLS_WORK.INPUT_GESTEMPR_ADENE_AUX1_DEZ24 AS

SELECT *
	,COUNT(*) OVER (PARTITION BY CONCAT(COLLATERALASSETID_ADENE,ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE,CREATIONDATE_ADENE)) AS NUM_REG_TRAT_3
FROM
	(
    SELECT *
		,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_ADENE,ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE) ORDER BY CREATIONDATE_ADENE DESC) AS RANK_TRAT_3
	FROM
		(
		SELECT *
			,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_ADENE,ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE) ORDER BY FLAG_CRUZA_ADENE_OCR DESC) AS RANK_TRAT_2
		FROM
			(
			SELECT *
				,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_ADENE,ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE) ORDER BY FLAG_CAMPOS_PREENC DESC) AS RANK_TRAT_1
			FROM
				(
				SELECT *
					,CASE WHEN TRIM(BASE_ADENE.EPC_ADENE)='' OR BASE_ADENE.EPC_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BASE_ADENE.EPC_EXPIRATION_DATE_ADENE)='' OR BASE_ADENE.EPC_EXPIRATION_DATE_ADENE IS NULL THEN 0 ELSE 1 END
						+ CASE WHEN TRIM(BASE_ADENE.ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE)='' OR BASE_ADENE.ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE IS NULL THEN 0 ELSE 1 END
						+ CASE WHEN TRIM(BUILD_CERT.CERTIFICATE_TYPE_ADENE)='' OR BUILD_CERT.CERTIFICATE_TYPE_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.PROPERTY_PURPOSE_CODE_ADENE)='' OR BUILD_CERT.PROPERTY_PURPOSE_CODE_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.LOCALITY_ADENE)='' OR BUILD_CERT.LOCALITY_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.NET_FLOOR_AREA_ADENE)='' OR BUILD_CERT.NET_FLOOR_AREA_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.REN_ENERGY_PRODUCED_VALUE_ADENE)='' OR BUILD_CERT.REN_ENERGY_PRODUCED_VALUE_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.CARBON_EMISSION_VALUE_YEAR_ADENE)='' OR BUILD_CERT.CARBON_EMISSION_VALUE_YEAR_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.NZEB_IND_ADENE)='' OR BUILD_CERT.NZEB_IND_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(BUILD_CERT.EPC_ISSUING_DATE_ADENE)='' OR BUILD_CERT.EPC_ISSUING_DATE_ADENE IS NULL THEN 0 ELSE 1 END  
						+ CASE WHEN TRIM(MAJOR_1.PRIMARY_ENERGY_REQ_VALUE_ADENE)='' OR MAJOR_1.PRIMARY_ENERGY_REQ_VALUE_ADENE IS NULL THEN 0 ELSE 1 END 
						+ CASE WHEN TRIM(MAJOR_2.ENERGY_CONSUMPTION_VALUE_YEAR_ADENE)='' OR MAJOR_2.ENERGY_CONSUMPTION_VALUE_YEAR_ADENE IS NULL THEN 0 ELSE 1 END
						+ CASE WHEN TRIM(BUILD_CERT.CURRENT_ENERGY_RATING_PERCENTAGE_ADENE)='' OR BUILD_CERT.CURRENT_ENERGY_RATING_PERCENTAGE_ADENE IS NULL THEN 0 ELSE 1 END						
					AS FLAG_CAMPOS_PREENC
					,CASE 
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='A+' THEN 1
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='A' THEN 2
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='A-' THEN 3
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='B+' THEN 4
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='B' THEN 5
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='B-' THEN 6
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='C' THEN 7
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='D' THEN 8
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='E' THEN 9
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='F' THEN 10
						WHEN TRIM(BASE_ADENE.EPC_ADENE)='G' THEN 11
					END AS FLAG_ORDEM_EPC 
					,CASE 
						WHEN OCR.ENERGY_CERTIFICATION_SYSTEM_CODE_OCR IS NOT NULL THEN 1
						ELSE 0
					END AS FLAG_CRUZA_ADENE_OCR
                    ,RANK() OVER (PARTITION BY BASE_ADENE.COLLATERALASSETID_ADENE ORDER BY COALESCE(BASE_ADENE.EPC_EXPIRATION_DATE_ADENE, '01-01-1900') DESC) AS RANK_DT_VALIDADE
                    FROM 
                    
                      	-- 1º Cruzamento: Informação base da Property Certificate 
                      	--> OUT24 110.742 Registos 
                        (
                      	SELECT 
              		        TRIM(ID) AS ID_ADENE
              		        ,TRIM(COLLATERALASSETID) AS COLLATERALASSETID_ADENE
              		        ,TRIM(GNID) AS GNID_ADENE
              		        ,'ADENE' AS SOURCE_CODE_ADENE
              		        ,TRIM(CREATIONDATE) AS CREATIONDATE_ADENE
              		        ,TRIM(OCRUPDATEDATE) AS OCRUPDATEDATE_ADENE
              		        ,TRIM(ENERGYEFFICIENCYRATING) AS EPC_ADENE
              		        ,TRIM(EPCREFERENCENUMBER) AS ENERGY_CERTIFICATION_SYSTEM_CODE_ADENE
              		        ,TRIM(EPCEXPIRATIONDATE) AS EPC_EXPIRATION_DATE_ADENE
              		        ,DATA_DATE_PART AS DATA_DATE_PART_ADENE 
							,DATA_TIMESTAMP_PART AS DATA_TIMESTAMP_PART_ADENE    
                      	FROM CURATED_INTERNAL_GESTAOIMOVEIS.PROPERTYCERTIFICATE
                      	WHERE TRIM(UPPER(ENERGYEFFICIENCYRATING)) NOT IN ('+','-','S','N','J','')
                      		AND TRIM(DOCUMENTTYPE)='ADENE' 
                      		AND DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CURATED_INTERNAL_GESTAOIMOVEIS.PROPERTYCERTIFICATE WHERE DATA_DATE_PART <= '${ref_date}')
