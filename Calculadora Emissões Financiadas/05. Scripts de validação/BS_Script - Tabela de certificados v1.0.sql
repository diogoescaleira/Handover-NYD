CREATE TABLE BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_STEP3_DEZ24 AS

SELECT *
	,CAST(REGEXP_REPLACE(REGEXP_REPLACE(F1.NET_FLOOR_AREA_OCR,'[^0-9,.]',''),',','.') AS DECIMAL(18,6))/F2.SUM_NET_FLOOR_AREA_OCR AS PERCENTAGEM
