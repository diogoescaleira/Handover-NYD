DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_DEZ24;
CREATE TABLE BU_CAPTOOLS_WORK.INPUT_GESTEMPR_OCR_DEZ24 AS

SELECT *
FROM
	(
	SELECT *
		,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_OCR,ENERGY_CERTIFICATION_SYSTEM_CODE_OCR) ORDER BY CAST(ID_OCR AS DECIMAL(18,6)) DESC) AS RANK_TRAT_4
	FROM
		(
		SELECT *
			,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_OCR,ENERGY_CERTIFICATION_SYSTEM_CODE_OCR) ORDER BY OCRUPDATEDATE_OCR DESC) AS RANK_TRAT_3
		FROM
			(
			SELECT *
				,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_OCR,ENERGY_CERTIFICATION_SYSTEM_CODE_OCR) ORDER BY CREATIONDATE_OCR DESC) AS RANK_TRAT_2
			FROM
				(
				SELECT *
					,RANK() OVER (PARTITION BY CONCAT(COLLATERALASSETID_OCR,ENERGY_CERTIFICATION_SYSTEM_CODE_OCR) ORDER BY FLAG_CAMPOS_PREENC DESC) AS RANK_TRAT_1
				FROM
					(
					SELECT *,
						CASE WHEN TRIM(BASE_OCR.EPC_OCR)='' OR BASE_OCR.EPC_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BASE_OCR.EPC_EXPIRATION_DATE_OCR)='' OR BASE_OCR.EPC_EXPIRATION_DATE_OCR IS NULL THEN 0 ELSE 1 END
							+ CASE WHEN TRIM(BASE_OCR.ENERGY_CERTIFICATION_SYSTEM_CODE_OCR)='' OR BASE_OCR.ENERGY_CERTIFICATION_SYSTEM_CODE_OCR IS NULL THEN 0 ELSE 1 END
							+ CASE WHEN TRIM(DOC_1.LAND_REGISTRY_OFFICE_CODE_OCR)='' OR DOC_1.LAND_REGISTRY_OFFICE_CODE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(DOC_1.PROPERTY_IDENTIFICATION_CODE_OCR)='' OR DOC_1.PROPERTY_IDENTIFICATION_CODE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(DOC_2.CADASTRAL_REGISTER_CODE_OCR)='' OR DOC_2.CADASTRAL_REGISTER_CODE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.CERTIFICATE_TYPE_OCR)='' OR BUILD_CERT.CERTIFICATE_TYPE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.PROPERTY_PURPOSE_CODE_OCR)='' OR BUILD_CERT.PROPERTY_PURPOSE_CODE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.LOCALITY_OCR)='' OR BUILD_CERT.LOCALITY_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.NET_FLOOR_AREA_OCR)='' OR BUILD_CERT.NET_FLOOR_AREA_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.REN_ENERGY_PRODUCED_VALUE_OCR)='' OR BUILD_CERT.REN_ENERGY_PRODUCED_VALUE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.CARBON_EMISSION_VALUE_YEAR_OCR)='' OR BUILD_CERT.CARBON_EMISSION_VALUE_YEAR_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.NZEB_IND_OCR)='' OR BUILD_CERT.NZEB_IND_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.EPC_ISSUING_DATE_OCR)='' OR BUILD_CERT.EPC_ISSUING_DATE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.GPS_LATITUDE_OCR)='' OR BUILD_CERT.GPS_LATITUDE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.GPS_LONGITUDE_OCR)='' OR BUILD_CERT.GPS_LONGITUDE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.DISTRICT_MUNICIP_PARISH_CODE_OCR)='' OR BUILD_CERT.DISTRICT_MUNICIP_PARISH_CODE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.FLOOR_NUMBER_OCR)='' OR BUILD_CERT.FLOOR_NUMBER_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.ADDRESS_CODE_OCR)='' OR BUILD_CERT.ADDRESS_CODE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(MAJOR_1.PRIMARY_ENERGY_REQ_VALUE_OCR)='' OR MAJOR_1.PRIMARY_ENERGY_REQ_VALUE_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(MAJOR_2.ENERGY_CONSUMPTION_VALUE_YEAR_OCR)='' OR MAJOR_2.ENERGY_CONSUMPTION_VALUE_YEAR_OCR IS NULL THEN 0 ELSE 1 END 
							+ CASE WHEN TRIM(BUILD_CERT.CURRENT_ENERGY_RATING_PERCENTAGE_OCR)='' OR BUILD_CERT.CURRENT_ENERGY_RATING_PERCENTAGE_OCR IS NULL THEN 0 ELSE 1 END -- Sem impacto: O campo nunca esta preenchido 
						AS FLAG_CAMPOS_PREENC
						,CASE 
							WHEN TRIM(BASE_OCR.EPC_OCR)='A+' THEN 1
							WHEN TRIM(BASE_OCR.EPC_OCR)='A' THEN 2
							WHEN TRIM(BASE_OCR.EPC_OCR)='A-' THEN 3
							WHEN TRIM(BASE_OCR.EPC_OCR)='B+' THEN 4
							WHEN TRIM(BASE_OCR.EPC_OCR)='B' THEN 5
							WHEN TRIM(BASE_OCR.EPC_OCR)='B-' THEN 6
							WHEN TRIM(BASE_OCR.EPC_OCR)='C' THEN 7
							WHEN TRIM(BASE_OCR.EPC_OCR)='D' THEN 8
							WHEN TRIM(BASE_OCR.EPC_OCR)='E' THEN 9
							WHEN TRIM(BASE_OCR.EPC_OCR)='F' THEN 10
							WHEN TRIM(BASE_OCR.EPC_OCR)='G' THEN 11
						END AS FLAG_ORDEM_EPC 
						,RANK() OVER (PARTITION BY BASE_OCR.COLLATERALASSETID_OCR ORDER BY COALESCE(BASE_OCR.EPC_EXPIRATION_DATE_OCR, '01-01-1900') DESC) AS RANK_DT_VALIDADE
						FROM 
						
							-- 1º Cruzamento: Informação base da Property Certificate 
							--> OUT24 110.742 Registos 
							(
							SELECT 
								TRIM(ID) AS ID_OCR
								,TRIM(COLLATERALASSETID) AS COLLATERALASSETID_OCR
								,TRIM(GNID) AS GNID_OCR
								,'OCR' AS SOURCE_CODE_OCR
								,TRIM(CREATIONDATE) AS CREATIONDATE_OCR
								,TRIM(OCRUPDATEDATE) AS OCRUPDATEDATE_OCR
								,TRIM(ENERGYEFFICIENCYRATING) AS EPC_OCR
								,TRIM(EPCREFERENCENUMBER) AS ENERGY_CERTIFICATION_SYSTEM_CODE_OCR
								,TRIM(EPCEXPIRATIONDATE) AS EPC_EXPIRATION_DATE_OCR
								,DATA_DATE_PART AS DATA_DATE_PART_OCR 
								,DATA_TIMESTAMP_PART AS DATA_TIMESTAMP_PART_OCR    
							FROM CURATED_INTERNAL_GESTAOIMOVEIS.PROPERTYCERTIFICATE
							WHERE TRIM(UPPER(ENERGYEFFICIENCYRATING)) NOT IN ('+','-','S','N','J','')
								AND TRIM(DOCUMENTTYPE)<>'ADENE' 
								AND DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CURATED_INTERNAL_GESTAOIMOVEIS.PROPERTYCERTIFICATE WHERE DATA_DATE_PART <= '${ref_date}')
							)BASE_OCR
						
						LEFT JOIN
							-- 2º Cruzamento: Informação da DOCUMENTS via chave ID da info da ADENE 
							--> OUT24 110.742 Registos 	
							(
							SELECT
								TRIM(BUILDINGENERGYCERTIFICATEID) AS BUILDINGENERGYCERTIFICATEID_OCR_DOC1
								,TRIM(ISSUERENTITY) AS LAND_REGISTRY_OFFICE_CODE_OCR
								,TRIM(DOCUMENTNUMBER) AS PROPERTY_IDENTIFICATION_CODE_OCR
							FROM curated_internal_gestaoimoveis.Documents
							WHERE DOCUMENTTYPEDESCRIPTION = 'Nº de inscrição na Conservatória'
							AND DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CURATED_INTERNAL_GESTAOIMOVEIS.DOCUMENTS WHERE DATA_DATE_PART <= '${ref_date}')
							)DOC_1 ON BASE_OCR.ID_OCR=DOC_1.BUILDINGENERGYCERTIFICATEID_OCR_DOC1
						
						LEFT JOIN 
							-- 3º Cruzamento: Informação da DOCUMENTS via chave ID da info da ADENE 
							--> OUT24 110.742 Registos 
							(
							SELECT
								TRIM(BUILDINGENERGYCERTIFICATEID) AS BUILDINGENERGYCERTIFICATEID_OCR_DOC2
								,TRIM(DOCUMENTNUMBER) AS CADASTRAL_REGISTER_CODE_OCR
								FROM curated_internal_gestaoimoveis.Documents
								WHERE DOCUMENTTYPEDESCRIPTION = 'Artigo Matricial nº'
									AND DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM curated_internal_gestaoimoveis.Documents WHERE DATA_DATE_PART <= '${ref_date}')
							)DOC_2 ON BASE_OCR.ID_OCR=DOC_2.BUILDINGENERGYCERTIFICATEID_OCR_DOC2
						
						LEFT JOIN
							-- 4º Cruzamento: Informação da BuildingEnergyCertificate via chave ID da info da ADENE 
							--> OUT24 110.742 Registos  
							(
							SELECT 
								TRIM(PROPERTYCERTIFICATEID) AS PROPERTYCERTIFICATEID_OCR
								,TRIM(TYPEDESCRIPTION) AS CERTIFICATE_TYPE_OCR
								,TRIM(BUILDINGPURPOSEDESCRIPTION) AS PROPERTY_PURPOSE_CODE_OCR
								,TRIM(BUILDINGGEOLOCATIONPOSTALADDRESSTOWNNAME) AS LOCALITY_OCR
								,TRIM(BUILTUPAREAQUANTITY) AS NET_FLOOR_AREA_OCR
								,TRIM(RENEWABLEENERGYCONTRIBUTIONVALUE) AS REN_ENERGY_PRODUCED_VALUE_OCR
								,TRIM(CURRENTCARBONFOOTPRINTVALUE) AS CARBON_EMISSION_VALUE_YEAR_OCR
								,TRIM(NEARZEROBUILDINGINDICATOR) AS NZEB_IND_OCR
								,TRIM(ISSUEDATE) AS EPC_ISSUING_DATE_OCR
								,TRIM(BUILDINGGEOLOCATIONLATITUDE) AS GPS_LATITUDE_OCR
								,TRIM(BUILDINGGEOLOCATIONLONGITUDE) AS GPS_LONGITUDE_OCR
								,TRIM(BUILDINGGEOLOCATIONPOSTALADDRESSCITYNAME) AS DISTRICT_MUNICIP_PARISH_CODE_OCR
								,TRIM(BUILDINGGEOLOCATIONPOSTALADDRESSUNITTYPE) AS FLOOR_NUMBER_OCR
								,TRIM(BUILDINGGEOLOCATIONPOSTALADDRESSFULLADDRESS) AS ADDRESS_CODE_OCR
								,TRIM(CURRENTEFFICIENCYRATINGPERCENTAGE) AS CURRENT_ENERGY_RATING_PERCENTAGE_OCR
							FROM CURATED_INTERNAL_GESTAOIMOVEIS.BUILDINGENERGYCERTIFICATE
							WHERE DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CURATED_INTERNAL_GESTAOIMOVEIS.BUILDINGENERGYCERTIFICATE WHERE DATA_DATE_PART <= '${ref_date}')
							)BUILD_CERT ON BASE_OCR.ID_OCR=BUILD_CERT.PROPERTYCERTIFICATEID_OCR
						LEFT JOIN
							-- 5º Cruzamento: Informação da MajorIndicators via chave ID da info da ADENE 
							--> OUT24 110.742 Registos
							(
							SELECT 
								TRIM(BUILDINGENERGYCERTIFICATEID) AS BUILDINGENERGYCERTIFICATEID_OCR_MAJOR1
								,TRIM(MAJORINDVALUE) AS PRIMARY_ENERGY_REQ_VALUE_OCR
							FROM CURATED_INTERNAL_GESTAOIMOVEIS.MAJORINDICATORS
							WHERE TYPECODE IN ('Ntc','IEE')  
								AND DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CURATED_INTERNAL_GESTAOIMOVEIS.MAJORINDICATORS WHERE DATA_DATE_PART <= '${ref_date}')
							)MAJOR_1 ON BASE_OCR.ID_OCR=MAJOR_1.BUILDINGENERGYCERTIFICATEID_OCR_MAJOR1
					
						LEFT JOIN
							-- 6º Cruzamento: Informação da MajorIndicators via chave ID da info da ADENE 
							--> OUT24 110.742 Registos
							(
							SELECT 
							TRIM(BUILDINGENERGYCERTIFICATEID) AS BUILDINGENERGYCERTIFICATEID_OCR_MAJOR2
							,TRIM(MAJORINDVALUE) AS  ENERGY_CONSUMPTION_VALUE_YEAR_OCR
							FROM CURATED_INTERNAL_GESTAOIMOVEIS.MAJORINDICATORS
							WHERE TYPECODE = 'IEE'
								AND DATA_DATE_PART IN (SELECT MAX (DATA_DATE_PART) FROM CURATED_INTERNAL_GESTAOIMOVEIS.MAJORINDICATORS WHERE DATA_DATE_PART <= '${ref_date}')
							)MAJOR_2 ON BASE_OCR.ID_OCR=MAJOR_2.BUILDINGENERGYCERTIFICATEID_OCR_MAJOR2
					)TRATAMENTO_0 WHERE RANK_DT_VALIDADE=1 
				)TRATAMENTO_1 WHERE RANK_TRAT_1=1
			)TRATAMENTO_2 WHERE RANK_TRAT_2=1
		)TRATAMENTO_3 WHERE RANK_TRAT_3=1
	)TRATAMENTO_4 WHERE RANK_TRAT_4=1
 ;
 
