                        )BUILD_CERT ON BASE_ADENE.ID_ADENE=BUILD_CERT.PROPERTYCERTIFICATEID_ADENE
          	        LEFT JOIN
          	        	-- 5º Cruzamento: Informação da MajorIndicators via chave ID da info da ADENE 
          	        	--> OUT24 110.742 Registos
                        (
                        SELECT 
							TRIM(BUILDINGENERGYCERTIFICATEID) AS BUILDINGENERGYCERTIFICATEID_ADENE_MAJOR1
							,TRIM(MAJORINDVALUE) AS PRIMARY_ENERGY_REQ_VALUE_ADENE
                        FROM CURATED_INTERNAL_GESTAOIMOVEIS.MAJORINDICATORS
                        WHERE TYPECODE IN ('Ntc','IEE')
