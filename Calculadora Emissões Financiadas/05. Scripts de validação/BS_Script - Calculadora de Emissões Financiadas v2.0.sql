			-- BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24_OP1 --> BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24_OP4 (Ou seja, alteramos o cenário 2A para opçăo com input de Espanha) // Năo refletido abaixo 


