        BASE.CKBALBEM=GT18_PA.CKBALBEM AND
        BASE.CKCTABEM=GT18_PA.CKCTABEM AND
        BASE.CKREFBEM=GT18_PA.CKREFBEM        
    LEFT JOIN
    (
        SELECT CEMPRESP,CKBALRES,CKCTARES,CKREFRESP,CEMPBEM,CKBALBEM,CKCTABEM,CKREFBEM,SUM(MAVALIA) AS SUM_MAVALIA,SUM(MAVALIAA) AS SUM_MAVALIAA
        FROM CD_GARANTIAS.GT018_RATEIO_AVAL
        WHERE REF_DATE='${REF_DATE}'
        GROUP BY CEMPRESP,CKBALRES,CKCTARES,CKREFRESP,CEMPBEM,CKBALBEM,CKCTABEM,CKREFBEM
