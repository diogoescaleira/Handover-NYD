CREATE TABLE BU_CAPTOOLS_WORK.MODESG_OUT_GRA_EMSS_FNCD_V2 AS 

SELECT 
	EMSS_FNCD.*,
	
    -- Cenário #1 - Metodologia sem tratamento aos rácio 
    -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 AS RATIO_OP1,
    -- (SCP1_EMSS)*(-AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1) AS EMISS_SCP1_OP1,
    -- (SCP2_EMSS)*(-AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1) AS EMISS_SCP2_OP1,
    (SCP1_EMSS+SCP2_EMSS)*(-AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1) AS EMISS_OP1,

    -- Cenário #2 - Metodologia com tratamento aos rácio e sem tratamento aos preços de aquisiçăo 
    CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END AS RATIO_OP2,
    -- (SCP1_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP1_OP2,
    -- (SCP2_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP2_OP2,
    (SCP1_EMSS+SCP2_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_OP2,
    
    -- Cenário #3 - Metodologia sem tratamento aos rácio mas com tratamento aos preços de aquisiçăo
    CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2 END AS RATIO_OP3,
    -- (SCP1_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2 END) AS EMISS_SCP1_OP3,
    -- (SCP2_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2 END) AS EMISS_SCP2_OP3,
    (SCP1_EMSS+SCP2_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP2 END) AS EMISS_OP3,
    
    -- Cenário #2A - Metodologia com tratamento aos rácio e sem tratamento aos preços de aquisiçăo // EPC'S REAL
    CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END AS RATIO_OP2A,
    -- (SCP1_EMSS_OP1)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP1_OP2A,
    -- (SCP2_EMSS_OP1)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP2_OP2A,
    (SCP1_EMSS_OP2A+SCP2_EMSS_OP2A)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_OP2A,
    
    -- Cenário #2B - Metodologia com tratamento aos rácio e sem tratamento aos preços de aquisiçăo 
    CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END AS RATIO_OP2B,
    -- (SCP1_EMSS_OP2)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP1_OP2B,
    -- (SCP2_EMSS_OP2)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP2_OP2B,
    (SCP1_EMSS_OP2B+SCP2_EMSS_OP2B)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_OP2B,
    
    -- Cenário #2C - Metodologia com tratamento aos rácio e sem tratamento aos preços de aquisiçăo 
    CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END AS RATIO_OP2C,
    -- (SCP1_EMSS_OP3)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP1_OP2C,
    -- (SCP2_EMSS_OP3)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP2_OP2C,
    (SCP1_EMSS_OP3C+SCP2_EMSS_OP3C)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_OP2C,

    -- Cenário #2D - Metodologia com tratamento aos rácio e sem tratamento aos preços de aquisiçăo 
    CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3 END AS RATIO_OP2D,
    -- (SCP1_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3 END) AS EMISS_SCP1_OP2D,
    -- (SCP2_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3 END) AS EMISS_SCP2_OP2D,
    (SCP1_EMSS+SCP2_EMSS)*(CASE WHEN -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3>1 THEN 1 ELSE -AMOUNT_CTO/SUM_PRECO_AQUISICAO_OP3 END) AS EMISS_OP2D,
    
--     CASE WHEN FLAG_BEM='MUG_IMO' THEN CAST((CAST(-AMOUNT /AMOUNT_CTO_COBERT AS DECIMAL(38,20)))*CAST(AMOUNT_CTO_NCOBERT AS DECIMAL(38,6)) AS DECIMAL(38,26)) END AS AUX_DQS,
-- 	SUM(CASE WHEN FLAG_BEM='MUG_IMO' THEN CAST((CAST(-AMOUNT /AMOUNT_CTO_COBERT AS DECIMAL(38,20)))*CAST(AMOUNT_CTO_NCOBERT AS DECIMAL(38,6)) AS DECIMAL(38,26)) END) OVER (PARTITION BY CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT)) AS SUM_AUX_DQS,
    
	CASE 
        WHEN FLAG_COBERT='SUM_COLAT_COBRE' THEN -AMOUNT*CAST(DQS AS DECIMAL(1,0))
        WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' AND AMOUNT_CTO_COBERT=0 THEN (CAST(DQS AS DECIMAL(38,6))/CAST(NUM_BENS AS DECIMAL(38,6)))*-AMOUNT_CTO_NCOBERT --31000010014986563000000000000000 // 89000000001015266000430000690446
		WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' THEN CAST((CAST(-AMOUNT /AMOUNT_CTO_COBERT AS DECIMAL(38,20)))*(CAST(AMOUNT_CTO_NCOBERT AS DECIMAL(38,6)))*(CAST(DQS AS DECIMAL(1,0))) AS DECIMAL(38,26))
    END AS DQS_CAL,
    CASE 
        WHEN FLAG_COBERT='SUM_COLAT_COBRE' THEN -AMOUNT*CAST(DQS_OP1 AS DECIMAL(1,0))
		WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' AND AMOUNT_CTO_COBERT=0 THEN (CAST(DQS_OP1 AS DECIMAL(38,6))/CAST(NUM_BENS AS DECIMAL(38,6)))*-AMOUNT_CTO_NCOBERT --31000010014986563000000000000000 // 89000000001015266000430000690446
        WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' THEN CAST((CAST(-AMOUNT /AMOUNT_CTO_COBERT AS DECIMAL(38,20)))*(CAST(AMOUNT_CTO_NCOBERT AS DECIMAL(38,6)))*(CAST(DQS_OP1 AS DECIMAL(1,0))) AS DECIMAL(38,26))
    END AS DQS_CAL_OP1,
    CASE 
        WHEN FLAG_COBERT='SUM_COLAT_COBRE' THEN -AMOUNT*CAST(DQS_OP2 AS DECIMAL(1,0))
		WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' AND AMOUNT_CTO_COBERT=0 THEN (CAST(DQS_OP2 AS DECIMAL(38,6))/CAST(NUM_BENS AS DECIMAL(38,6)))*-AMOUNT_CTO_NCOBERT --31000010014986563000000000000000 // 89000000001015266000430000690446
        WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' THEN CAST((CAST(-AMOUNT /AMOUNT_CTO_COBERT AS DECIMAL(38,20)))*(CAST(AMOUNT_CTO_NCOBERT AS DECIMAL(38,6)))*(CAST(DQS_OP2 AS DECIMAL(1,0))) AS DECIMAL(38,26))
    END AS DQS_CAL_OP2,
    CASE 
        WHEN FLAG_COBERT='SUM_COLAT_COBRE' THEN -AMOUNT*CAST(DQS_OP3 AS DECIMAL(1,0))
		WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' AND AMOUNT_CTO_COBERT=0 THEN (CAST(DQS_OP3 AS DECIMAL(38,6))/CAST(NUM_BENS AS DECIMAL(38,6)))*-AMOUNT_CTO_NCOBERT --31000010014986563000000000000000 // 89000000001015266000430000690446
        WHEN FLAG_COBERT='SUM_COLAT_N_COBRE' THEN CAST((CAST(-AMOUNT /AMOUNT_CTO_COBERT AS DECIMAL(38,20)))*(CAST(AMOUNT_CTO_NCOBERT AS DECIMAL(38,6)))*(CAST(DQS_OP3 AS DECIMAL(1,0))) AS DECIMAL(38,26)) 
		--'31000010040823563000000000000000'
		--'31000010040823563000000000000000'		
    END AS DQS_CAL_OP3,
    
	-- PARTIÇĂO DA TABELA 
    RT.NEW_ID_CORRIDA AS ID_CORRIDA,
    '${REF_DATE}' AS REF_DATE
FROM
(
    SELECT 
        BASE.*,
		CASE
			WHEN TRIM(PURPOSE_CODE) IN ('Habitacao') THEN 'RRE'
			WHEN TRIM(PURPOSE_CODE) IN ('CRE_OUTRO') THEN 'CRE'
		ELSE ''
		END AS FLG_CRE_RRE,
        CT004.PURPOSE_CODE,
        CONCAT(CT004.CPRODUTO,CT004.CSUBPROD) AS PRODUTO,
        -- Cenário #1 e #2 e #3
        BENS_IMOV.ORIGEM_SCP1_EMSS,
        BENS_IMOV.ORIGEM_SCP2_EMSS,
        CASE 
            WHEN BENS_IMOV.ORIGEM_SCP1_EMSS LIKE '%DQS3%' THEN 3 
            WHEN BENS_IMOV.ORIGEM_SCP1_EMSS LIKE '%DQS4%' THEN 4
            WHEN BENS_IMOV.ORIGEM_SCP1_EMSS LIKE '%DQS5%' THEN 5 
        END AS DQS,
        BENS_IMOV.SCP1_EMSS, 
        BENS_IMOV.SCP2_EMSS,
        -- Cenário #2A
        BENS_IMOV_OP1.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_OP2A,
        BENS_IMOV_OP1.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_OP2A,
        CASE 
            WHEN BENS_IMOV_OP1.ORIGEM_SCP1_EMSS LIKE '%DQS3%' THEN 3 
            WHEN BENS_IMOV_OP1.ORIGEM_SCP1_EMSS LIKE '%DQS4%' THEN 4
            WHEN BENS_IMOV_OP1.ORIGEM_SCP1_EMSS LIKE '%DQS5%' THEN 5 
        END AS DQS_OP1,
        BENS_IMOV_OP1.SCP1_EMSS AS SCP1_EMSS_OP2A, 
        BENS_IMOV_OP1.SCP2_EMSS AS SCP2_EMSS_OP2A,
		-- Cenário #2B
        BENS_IMOV_OP2.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_OP2B,
        BENS_IMOV_OP2.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_OP2B,
        CASE 
            WHEN BENS_IMOV_OP2.ORIGEM_SCP1_EMSS LIKE '%DQS3%' THEN 3 
            WHEN BENS_IMOV_OP2.ORIGEM_SCP1_EMSS LIKE '%DQS4%' THEN 4
            WHEN BENS_IMOV_OP2.ORIGEM_SCP1_EMSS LIKE '%DQS5%' THEN 5 
        END AS DQS_OP2,
        BENS_IMOV_OP2.SCP1_EMSS AS SCP1_EMSS_OP2B, 
        BENS_IMOV_OP2.SCP2_EMSS AS SCP2_EMSS_OP2B,
	    -- Cenário #2C
        BENS_IMOV_OP3.ORIGEM_SCP1_EMSS AS ORIGEM_SCP1_EMSS_OP3C,
        BENS_IMOV_OP3.ORIGEM_SCP2_EMSS AS ORIGEM_SCP2_EMSS_OP3C,
        CASE 
            WHEN BENS_IMOV_OP3.ORIGEM_SCP1_EMSS LIKE '%DQS3%' THEN 3 
            WHEN BENS_IMOV_OP3.ORIGEM_SCP1_EMSS LIKE '%DQS4%' THEN 4
            WHEN BENS_IMOV_OP3.ORIGEM_SCP1_EMSS LIKE '%DQS5%' THEN 5 
        END AS DQS_OP3,
        BENS_IMOV_OP3.SCP1_EMSS AS SCP1_EMSS_OP3C, 
        BENS_IMOV_OP3.SCP2_EMSS AS SCP2_EMSS_OP3C,
        PROP.PROPERTY_PURPOSE_CODE,
        PROP.NET_FLOOR_AREA,
        PROP.COUNTRY_CODE,
        PROP_ENER.EPC, 
        PROP_ENER.QUALITY_SCORE,
        PROP_ENER.SOURCE_CODE,
        PROP_ENER_ATUAL.EPC AS EPC_ATUALIZ,
        PROP_ENER_ATUAL.QUALITY_SCORE AS QUALITY_SCORE_ATUALIZ,
        PROP_ENER_ATUAL.SOURCE_CODE AS SOURCE_CODE_ATUALIZ,
        PROP_ENER_ATUAL.DATA_DATE_PART AS DATA_DATE_PART_ATUALIZ,
        PROP_ENER_ATUAL.EPC_ISSUING_DATE,
        PROP_ENER_ATUAL.EPC_EXPIRATION_DATE,
        GT18.CGARANT_BEM,
        CONTRAPARTE,
		GT18_PA.PRECO_AQUISICAO AS PRECO_AQUISICAO_ORI,
		GT18_AGR.SUM_MAVALIA,
		GT18_AGR.SUM_MAVALIAA,
        CASE
            WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO<>0 THEN GT18_PA.PRECO_AQUISICAO
            WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO=0) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
            ELSE GT18_AGR.SUM_MAVALIAA
        END AS PRECO_AQUISICAO_OP1,
        SUM(
            CASE
                WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO<>0 THEN GT18_PA.PRECO_AQUISICAO
                WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO=0) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
                ELSE GT18_AGR.SUM_MAVALIAA
            END
            ) OVER (PARTITION BY CONCAT(BASE.CEMPRESA,BASE.CBALCAO,BASE.CNUMECTA,BASE.ZDEPOSIT)) AS SUM_PRECO_AQUISICAO_OP1,
        CASE
            WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO>100 THEN GT18_PA.PRECO_AQUISICAO
            WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO<=100) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
            ELSE GT18_AGR.SUM_MAVALIAA
        END AS PRECO_AQUISICAO_OP2,
        SUM(
            CASE
                WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO>100 THEN GT18_PA.PRECO_AQUISICAO
                WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO<=100) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
                ELSE GT18_AGR.SUM_MAVALIAA
            END
            ) OVER (PARTITION BY CONCAT(BASE.CEMPRESA,BASE.CBALCAO,BASE.CNUMECTA,BASE.ZDEPOSIT)) AS SUM_PRECO_AQUISICAO_OP2,
            
        CASE
            WHEN GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
            ELSE GT18_AGR.SUM_MAVALIAA
        END AS PRECO_AQUISICAO_OP3,
        SUM(
            CASE
                WHEN GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
                ELSE GT18_AGR.SUM_MAVALIAA
            END
            ) OVER (PARTITION BY CONCAT(BASE.CEMPRESA,BASE.CBALCAO,BASE.CNUMECTA,BASE.ZDEPOSIT)) AS SUM_PRECO_AQUISICAO_OP3,
        CASE
            WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO<>0 THEN 1
            WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO=0) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN 2
            ELSE 3
        END AS FLAG_PRECO_AQUISICAO
    FROM 
    (
    SELECT AUX1.*,AUX2.AMOUNT_CTO_COBERT,AUX2.AMOUNT_CTO_NCOBERT,
		CASE WHEN BEM_CTO.FLAG_BEM='MUG_IMO' THEN 'CTO_IMO' WHEN BEM_CTO.FLAG_BEM='MUG_MOV' THEN 'CTO_MOV' ELSE 'SEM COLATERAL' END AS FLAG_CTO_BEM
    FROM    
        (
            SELECT   CEMPRESA
                    ,CBALCAO
                    ,CNUMECTA
                    ,ZDEPOSIT
                    ,CEMPBEM
                    ,CKBALBEM
                    ,CKCTABEM
                    ,CKREFBEM
                    ,FLAG_COBERT
                    ,FLAG_BEM
					,NUM_BENS
                    ,SUM_MSALDO_FINAL_CT AS AMOUNT_CTO
                    ,AMOUNT
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
            WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
        )AUX1 
        LEFT JOIN 
        (
            SELECT   CEMPRESA
                    ,CBALCAO
                    ,CNUMECTA
                    ,ZDEPOSIT
                    ,SUM(CASE WHEN (CKCTABEM IS NOT NULL AND FLAG_COBERT='SUM_COLAT_N_COBRE') OR FLAG_COBERT='SUM_COLAT_COBRE'  THEN AMOUNT END) AS AMOUNT_CTO_COBERT
                    ,SUM(CASE WHEN (CKCTABEM IS NULL AND FLAG_COBERT='SUM_COLAT_N_COBRE')  THEN AMOUNT END) AS AMOUNT_CTO_NCOBERT
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
            WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
            GROUP BY 1,2,3,4
        )AUX2
        ON  AUX1.CEMPRESA = AUX2.CEMPRESA AND
            AUX1.CBALCAO = AUX2.CBALCAO AND
            AUX1.CNUMECTA = AUX2.CNUMECTA AND
            AUX1.ZDEPOSIT = AUX2.ZDEPOSIT 
		LEFT JOIN 
		(
			SELECT DISTINCT CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT,FLAG_BEM
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
            WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
				AND CKBALBEM IS NOT NULL 
		)BEM_CTO
			ON  AUX1.CEMPRESA = BEM_CTO.CEMPRESA AND
				AUX1.CBALCAO = BEM_CTO.CBALCAO AND
				AUX1.CNUMECTA = BEM_CTO.CNUMECTA AND
				AUX1.ZDEPOSIT = BEM_CTO.ZDEPOSIT 
    )BASE
    LEFT JOIN
    (
     SELECT *
     FROM CD_CAPTOOLS.CT004_UNIV_CTO
     WHERE REF_DATE='${REF_DATE}'
    ) CT004
    ON  BASE.CEMPRESA = CT004.CEMPRESA AND
        BASE.CBALCAO = CT004.CBALCAO AND
        BASE.CNUMECTA = CT004.CNUMECTA AND
        BASE.ZDEPOSIT = CT004.ZDEPOSIT
    LEFT JOIN
    (
        SELECT *
        FROM BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24
    ) BENS_IMOV 
    ON  BASE.CEMPBEM=BENS_IMOV.CEMPBEM AND
        BASE.CKBALBEM=BENS_IMOV.CKBALBEM AND
        BASE.CKCTABEM=BENS_IMOV.CKCTABEM AND
        BASE.CKREFBEM=BENS_IMOV.CKREFBEM 
    LEFT JOIN
    (
        SELECT *
        FROM BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24_OP1
    ) BENS_IMOV_OP1 
    ON  BASE.CEMPBEM=BENS_IMOV_OP1.CEMPBEM AND
        BASE.CKBALBEM=BENS_IMOV_OP1.CKBALBEM AND
        BASE.CKCTABEM=BENS_IMOV_OP1.CKCTABEM AND
        BASE.CKREFBEM=BENS_IMOV_OP1.CKREFBEM 
    LEFT JOIN
    (
        SELECT *
        FROM BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24_OP2
    ) BENS_IMOV_OP2 
    ON  BASE.CEMPBEM=BENS_IMOV_OP2.CEMPBEM AND
        BASE.CKBALBEM=BENS_IMOV_OP2.CKBALBEM AND
        BASE.CKCTABEM=BENS_IMOV_OP2.CKCTABEM AND
        BASE.CKREFBEM=BENS_IMOV_OP2.CKREFBEM 
    LEFT JOIN
    (
        SELECT *
        FROM BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24_OP3
    ) BENS_IMOV_OP3 
    ON  BASE.CEMPBEM=BENS_IMOV_OP3.CEMPBEM AND
        BASE.CKBALBEM=BENS_IMOV_OP3.CKBALBEM AND
        BASE.CKCTABEM=BENS_IMOV_OP3.CKCTABEM AND
        BASE.CKREFBEM=BENS_IMOV_OP3.CKREFBEM 
    LEFT JOIN
    (
        SELECT *
        FROM BUSINESS_ASSETS.PROPERTY
        WHERE DATA_DATE_PART='${REF_DATE}' 
    ) PROP 
    ON  BASE.CEMPBEM=PROP.PROPERTY_BANK_ID AND
        BASE.CKBALBEM=PROP.PROPERTY_BRANCH_CODE AND
        BASE.CKCTABEM=PROP.PROPERTY_CONTRACT_ID AND
        BASE.CKREFBEM=PROP.PROPERTY_REFERENCE_CODE
    LEFT JOIN
    (
        SELECT *
