	-- Controlo #1 - Controlo da finalidade do bem, e valores de emissăo a null 
	
SELECT 
    CASE 
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
        WHEN TRIM(PROPERTY_PURPOSE_CODE)=TRIM(CFINBEM) AND PROPERTY_PURPOSE_CODE IS NOT NULL AND CFINBEM IS NOT NULL THEN 'OK'
        ELSE 'NOK'
    END AS VALID_FIN_BEM 
    ,CASE
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
        WHEN EMSS_FNC_SCP1 IS NULL AND TRIM(PROPERTY_PURPOSE_CODE) IN ('06','07') THEN 'OK'
        WHEN EMSS_FNC_SCP1 IS NULL AND ORIGEM_SCP1_EMSS='MISS' THEN 'OK'
        WHEN EMSS_FNC_SCP1 IS NULL AND PRECO_AQUISICAO=0 THEN 'OK'
        WHEN EMSS_FNC_SCP1 IS NOT NULL THEN 'OK'
        ELSE 'NOK'
    END AS SCP1_FINAL
    ,CASE
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
        WHEN EMSS_FNC_SCP2 IS NULL AND TRIM(PROPERTY_PURPOSE_CODE) IN ('06','07') THEN 'OK'
        WHEN EMSS_FNC_SCP2 IS NULL AND ORIGEM_SCP2_EMSS='MISS' THEN 'OK'
        WHEN EMSS_FNC_SCP2 IS NULL AND PRECO_AQUISICAO=0 THEN 'OK'
        WHEN EMSS_FNC_SCP2 IS NOT NULL THEN 'OK'
        ELSE 'NOK'
    END AS SCP2_FINAL
    ,CASE 
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
        WHEN ORIGEM_SCP1_EMSS='Nao Aplicavel' AND (TRIM(PROPERTY_PURPOSE_CODE) NOT IN ('06','07') OR PROPERTY_PURPOSE_CODE IS NULL) THEN 'NOK'
        ELSE 'OK'
    END AS SCP1_ORIGEM_FINAL
    ,CASE 
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
        WHEN ORIGEM_SCP2_EMSS='Nao Aplicavel' AND (TRIM(PROPERTY_PURPOSE_CODE) NOT IN ('06','07') OR PROPERTY_PURPOSE_CODE IS NULL) THEN 'NOK'
        ELSE 'OK'
    END AS SCP2_ORIGEM_FINAL
	,CASE 
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
		WHEN TRIM(ORIGEM_SCP1_EMSS)='RE_DQS3_2A' AND (QUALITY_SCORE NOT IN  ('SANTANDER','1-REAL') OR QUALITY_SCORE IS NULL) THEN 'NOK'
		ELSE 'OK'
	END AS SCP1_ORIGEM_EPC_REAL
	,CASE 
		WHEN F1.CKCTABEM IS NULL OR F1.FLAG_BEM='MUG_MOV' THEN 'OK'
		WHEN TRIM(ORIGEM_SCP2_EMSS)='RE_DQS3_2A' AND (QUALITY_SCORE NOT IN  ('SANTANDER','1-REAL') OR QUALITY_SCORE IS NULL) THEN 'NOK'
		ELSE 'OK'
	END AS SCP2_ORIGEM_EPC_REAL
    ,SUM(EMSS_FNC_SCP1) AS SUM_EMSS_FNC_SCP1
    ,SUM(EMSS_FNC_SCP2) AS SUM_EMSS_FNC_SCP2
    ,COUNT(*) AS NUM_REG
    
FROM 
    (
    SELECT *,EMISS_SCP1_OP1 AS EMSS_FNC_SCP1, EMISS_SCP2_OP1 AS EMSS_FNC_SCP2,SUM_PRECO_AQUISICAO_OP1 AS PRECO_AQUISICAO
    FROM BU_CAPTOOLS_WORK.CAL_EMI_GRA
    WHERE REF_DATE='${REF_DATE}'
    AND ID_CORRIDA IN (SELECT MAX(ID_CORRIDA) FROM BU_CAPTOOLS_WORK.CAL_EMI_GRA WHERE REF_DATE='${REF_DATE}')
    )F1 
LEFT JOIN 
    (
    SELECT CEMPBEM,CKBALBEM,CKCTABEM,CKREFBEM,CFINBEM
    FROM CD_GARANTIAS.GT009_BENS_IMOV
    WHERE REF_DATE='${REF_DATE}'
    )F2 
    ON  F1.CEMPBEM=F2.CEMPBEM AND
        F1.CKBALBEM=F2.CKBALBEM AND
        F1.CKCTABEM=F2.CKCTABEM AND
        F1.CKREFBEM=F2.CKREFBEM  
GROUP BY 1,2,3,4,5,6,7
;

	-- Controlo #2 - Valores cobertos e năo cobertos  
	
SELECT * FROM
(
SELECT AUX1.*,AUX2.AMOUNT_CTO_COBERT,AUX2.AMOUNT_CTO_NCOBERT
FROM    
    (
        SELECT   CEMPRESA
                ,CBALCAO
                ,CNUMECTA
                ,ZDEPOSIT
                ,CEMPBEM
                ,CKBALBEM
                ,CKCTABEM
                ,CKREFBEM
                ,FLAG_COBERT
                ,SUM_MSALDO_FINAL_CT AS AMOUNT_CTO
                ,AMOUNT
        FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
        WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
    )AUX1 
    LEFT JOIN 
    (
        SELECT   CEMPRESA
                ,CBALCAO
                ,CNUMECTA
                ,ZDEPOSIT
                ,SUM(CASE WHEN (CKCTABEM IS NOT NULL AND FLAG_COBERT='SUM_COLAT_N_COBRE') OR FLAG_COBERT='SUM_COLAT_COBRE'  THEN AMOUNT END) AS AMOUNT_CTO_COBERT
                ,SUM(CASE WHEN (CKCTABEM IS NULL AND FLAG_COBERT='SUM_COLAT_N_COBRE')  THEN AMOUNT END) AS AMOUNT_CTO_NCOBERT
        FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
        WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
        GROUP BY 1,2,3,4
    )AUX2
    ON  AUX1.CEMPRESA = AUX2.CEMPRESA AND
        AUX1.CBALCAO = AUX2.CBALCAO AND
        AUX1.CNUMECTA = AUX2.CNUMECTA AND
        AUX1.ZDEPOSIT = AUX2.ZDEPOSIT 
)AUX WHERE ROUND(NVL(AMOUNT_CTO_COBERT,0)+NVL(AMOUNT_CTO_NCOBERT,0),2)<>ROUND(NVL(AMOUNT_CTO,0),2) AND FLAG_COBERT='SUM_COLAT_COBRE'
;

	-- Controlo #3 - Duplicados   

SELECT CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT,CEMPBEM,CKBALBEM,CKCTABEM,CKREFBEM) AS CHAVE, COUNT(*) AS NUM_REG
