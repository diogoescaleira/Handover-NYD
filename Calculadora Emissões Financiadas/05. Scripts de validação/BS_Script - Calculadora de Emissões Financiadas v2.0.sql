    ON  BASE.CEMPRESA = GT18_AGR.CEMPRESP AND
        BASE.CBALCAO = GT18_AGR.CKBALRES AND
        BASE.CNUMECTA = GT18_AGR.CKCTARES AND
        BASE.ZDEPOSIT = GT18_AGR.CKREFRESP AND
        BASE.CEMPBEM=GT18_AGR.CEMPBEM AND
        BASE.CKBALBEM=GT18_AGR.CKBALBEM AND
        BASE.CKCTABEM=GT18_AGR.CKCTABEM AND
        BASE.CKREFBEM=GT18_AGR.CKREFBEM
) EMSS_FNCD

