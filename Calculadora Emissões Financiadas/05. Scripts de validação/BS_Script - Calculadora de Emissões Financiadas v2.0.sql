WHERE REF_DATE='${REF_DATE}'
    AND ID_CORRIDA IN (SELECT MAX(ID_CORRIDA) FROM BU_CAPTOOLS_WORK.CAL_EMI_GRA WHERE REF_DATE='${REF_DATE}')
GROUP BY 1 HAVING NUM_REG>1 AND CHAVE IS NOT NULL 
;

	-- Controlo #4 - Tipologia de Bens associado ao Financiamento    

SELECT CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT) AS CHAVE, COUNT(DISTINCT FLAG_BEM) AS NUM_REG
FROM BU_CAPTOOLS_WORK.CAL_EMI_GRA
WHERE REF_DATE='${REF_DATE}'
    AND ID_CORRIDA IN (SELECT MAX(ID_CORRIDA) FROM BU_CAPTOOLS_WORK.CAL_EMI_GRA WHERE REF_DATE='${REF_DATE}')
GROUP BY 1 HAVING NUM_REG>1 AND CHAVE IS NOT NULL 

