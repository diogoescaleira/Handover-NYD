INSERT OVERWRITE TABLE BU_CAPTOOLS_WORK.CAL_EMI_GRA PARTITION (ID_CORRIDA,REF_DATE)

SELECT 
	EMSS_FNCD.*,
	CASE
		WHEN TRIM(PURPOSE_CODE) IN ('Habitacao') THEN 'RRE'
		WHEN TRIM(PURPOSE_CODE) IN ('CRE_OUTRO') THEN 'CRE'
	ELSE ''
	END AS FLG_CRE_RRE,
	
    -- Cenário #1 - Metodologia sem tratamento aos rácio 
    -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1 AS RATIO_OP1,
    (SCP1_EMSS)*(-BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1) AS EMISS_SCP1_OP1,
    (SCP2_EMSS)*(-BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1) AS EMISS_SCP2_OP1,
    (SCP1_EMSS+SCP2_EMSS)*(-BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1) AS EMISS_OP1,

    -- Cenário #2 - Metodologia sem tratamento aos rácio e sem tratamento aos preços de aquisição 
    CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1 END AS RATIO_OP2,
    (SCP1_EMSS)*(CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP1_OP2,
    (SCP2_EMSS)*(CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_SCP2_OP2,
    (SCP1_EMSS+SCP2_EMSS)*(CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP1 END) AS EMISS_OP2,
    
    -- Cenário #3 - Metodologia sem tratamento aos rácio mas com tratamento aos preços de aquisição
    CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2 END AS RATIO_OP3,
    (SCP1_EMSS)*(CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2 END) AS EMISS_SCP1_OP3,
    (SCP2_EMSS)*(CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2 END) AS EMISS_SCP2_OP3,
    (SCP1_EMSS+SCP2_EMSS)*(CASE WHEN -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2>1 THEN 1 ELSE -BASE.AMOUNT_CTO/PA.SUM_PRECO_AQUISICAO_OP2 END) AS EMISS_OP3,
	
	-- PARTIÇÃO DA TABELA 
    RT.NEW_ID_CORRIDA AS ID_CORRIDA,
    '${REF_DATE}' AS REF_DATE
FROM
(
    SELECT 
        BASE.*,
        CT004.PURPOSE_CODE,
        CONCAT(CT004.CPRODUTO,CT004.CSUBPROD) AS PRODUTO,
        SCP1_EMSS, 
        SCP2_EMSS,
        PROP.PROPERTY_PURPOSE_CODE,
        PROP.NET_FLOOR_AREA,
        PROP.COUNTRY_CODE,
        PROP_ENER.EPC, 
        PROP_ENER.QUALITY_SCORE,
        GT18.CGARANT_BEM,
        CONTRAPARTE,
		GT18_PA.PRECO_AQUISICAO AS PRECO_AQUISICAO_ORI,
		GT18_AGR.SUM_MAVALIA,
		GT18_AGR.SUM_MAVALIAA,
        CASE
            WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO<>0 THEN GT18_PA.PRECO_AQUISICAO
            WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO=0) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
            ELSE GT18_AGR.SUM_MAVALIAA
        END AS PRECO_AQUISICAO_OP1,
        SUM(
            CASE
                WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO<>0 THEN GT18_PA.PRECO_AQUISICAO
                WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO=0) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
                ELSE GT18_AGR.SUM_MAVALIAA
            END
            ) OVER (PARTITION BY CONCAT(BASE.CEMPRESA,BASE.CBALCAO,BASE.CNUMECTA,BASE.ZDEPOSIT)) AS SUM_PRECO_AQUISICAO_OP1,
        CASE
            WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO>100 THEN GT18_PA.PRECO_AQUISICAO
            WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO<=100) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
            ELSE GT18_AGR.SUM_MAVALIAA
        END AS PRECO_AQUISICAO_OP2,
        SUM(
            CASE
                WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO>100 THEN GT18_PA.PRECO_AQUISICAO
                WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO<=100) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN GT18_AGR.SUM_MAVALIA
                ELSE GT18_AGR.SUM_MAVALIAA
            END
            ) OVER (PARTITION BY CONCAT(BASE.CEMPRESA,BASE.CBALCAO,BASE.CNUMECTA,BASE.ZDEPOSIT)) AS SUM_PRECO_AQUISICAO_OP2,
        CASE
            WHEN GT18_PA.PRECO_AQUISICAO IS NOT NULL AND GT18_PA.PRECO_AQUISICAO<>0 THEN 1
            WHEN (GT18_PA.PRECO_AQUISICAO IS NULL OR GT18_PA.PRECO_AQUISICAO=0) AND GT18_AGR.SUM_MAVALIA IS NOT NULL THEN 2
            ELSE 3
        END AS FLAG_PRECO_AQUISICAO,
        BENS_IMOV.ORIGEM_SCP1_EMSS,
        BENS_IMOV.ORIGEM_SCP2_EMSS
    FROM 
    (
    SELECT AUX1.*,AUX2.AMOUNT_CTO_COBERT,AUX2.AMOUNT_CTO_NCOBERT,
		CASE WHEN BEM_CTO.FLAG_BEM='MUG_IMO' THEN 'CTO_IMO' WHEN BEM_CTO.FLAG_BEM='MUG_MOV' THEN 'CTO_MOV' ELSE 'SEM COLATERAL' END AS FLAG_CTO_BEM,
    FROM    
        (
            SELECT   CEMPRESA
