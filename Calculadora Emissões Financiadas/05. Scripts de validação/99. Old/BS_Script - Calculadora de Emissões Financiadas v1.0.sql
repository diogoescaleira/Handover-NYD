        BASE.CKBALBEM=PROP_ENER.PROPERTY_BRANCH_CODE AND
        BASE.CKCTABEM=PROP_ENER.PROPERTY_CONTRACT_ID AND
        BASE.CKREFBEM=PROP_ENER.PROPERTY_REFERENCE_CODE        
    LEFT JOIN  
