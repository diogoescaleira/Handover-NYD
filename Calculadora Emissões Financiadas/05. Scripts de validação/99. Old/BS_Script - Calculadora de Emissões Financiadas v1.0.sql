                    ,CNUMECTA
                    ,ZDEPOSIT
                    ,CEMPBEM
                    ,CKBALBEM
                    ,CKCTABEM
                    ,CKREFBEM
                    ,FLAG_COBERT
                    ,FLAG_BEM
					,NUM_BENS
                    ,SUM_MSALDO_FINAL_CT AS AMOUNT_CTO
                    ,AMOUNT
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
            WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
        )AUX1 
        LEFT JOIN 
        (
            SELECT   CEMPRESA
                    ,CBALCAO
                    ,CNUMECTA
                    ,ZDEPOSIT
                    ,SUM(CASE WHEN (CKCTABEM IS NOT NULL AND FLAG_COBERT='SUM_COLAT_N_COBRE') OR FLAG_COBERT='SUM_COLAT_COBRE'  THEN AMOUNT END) AS AMOUNT_CTO_COBERT
                    ,SUM(CASE WHEN (CKCTABEM IS NULL AND FLAG_COBERT='SUM_COLAT_N_COBRE')  THEN AMOUNT END) AS AMOUNT_CTO_NCOBERT
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
            WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
            GROUP BY 1,2,3,4
        )AUX2
        ON  AUX1.CEMPRESA = AUX2.CEMPRESA AND
            AUX1.CBALCAO = AUX2.CBALCAO AND
            AUX1.CNUMECTA = AUX2.CNUMECTA AND
            AUX1.ZDEPOSIT = AUX2.ZDEPOSIT 
		LEFT JOIN 
		(
			SELECT DISTINCT CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT,FLAG_BEM
            FROM BU_CAPTOOLS_WORK.EMI_FINAN_AUX3
            WHERE FLAG_PERIMETRO<>'Nao Aplicavel'
				AND CKBALBEM IS NOT NULL 
		)BEM_CTO
			ON  AUX1.CEMPRESA = BEM_CTO.CEMPRESA AND
				AUX1.CBALCAO = BEM_CTO.CBALCAO AND
				AUX1.CNUMECTA = BEM_CTO.CNUMECTA AND
				AUX1.ZDEPOSIT = BEM_CTO.ZDEPOSIT 
    )BASE
    LEFT JOIN
    (
     SELECT *
     FROM CD_CAPTOOLS.CT004_UNIV_CTO
     WHERE REF_DATE='${REF_DATE}'
    ) CT004
    ON  BASE.CEMPRESA = CT004.CEMPRESA AND
        BASE.CBALCAO = CT004.CBALCAO AND
        BASE.CNUMECTA = CT004.CNUMECTA AND
        BASE.ZDEPOSIT = CT004.ZDEPOSIT
    LEFT JOIN
    (
        SELECT *
        FROM BU_CAPTOOLS_WORK.BENS_IMOVEIS_MV_DEZ24
    ) BENS_IMOV 
    ON  BASE.CEMPBEM=BENS_IMOV.CEMPBEM AND
        BASE.CKBALBEM=BENS_IMOV.CKBALBEM AND
        BASE.CKCTABEM=BENS_IMOV.CKCTABEM AND
        BASE.CKREFBEM=BENS_IMOV.CKREFBEM    
    LEFT JOIN
    (
        SELECT *
        FROM BUSINESS_ASSETS.PROPERTY
        WHERE DATA_DATE_PART='${REF_DATE}' 
