DROP TABLE IF EXISTS BU_CAPTOOLS_WORK.SIMUL_GD_MIS_ABR_MAI_24;
CREATE TABLE BU_CAPTOOLS_WORK.SIMUL_GD_MIS_ABR_MAI_24 AS

SELECT
	*
	,CASE
		WHEN X.CHAVE_MIS_207_MAI IS NOT NULL THEN X.CHAVE_MIS_207_MAI
		WHEN Y.CHAVE_MIS_207_ABR IS NOT NULL THEN Y.CHAVE_MIS_207_ABR
	END AS CHAVE
	,CASE
		WHEN X.CHAVE_MIS_207_MAI IS NOT NULL THEN X.FLAG_BUS_AREA_MAI
		WHEN Y.CHAVE_MIS_207_ABR IS NOT NULL THEN Y.FLAG_BUS_AREA_ABR
	END AS FLAG_BUS_AREA
    ,CASE
        WHEN X.CHAVE_MIS_207_MAI IS NOT NULL AND Y.CHAVE_MIS_207_ABR IS NOT NULL THEN 'UNIVERSO COMUM'
        WHEN X.CHAVE_MIS_207_MAI IS NOT NULL AND Y.CHAVE_MIS_207_ABR IS NULL THEN 'UNIVERSO EXCLUSIVO MAIO'
        WHEN X.CHAVE_MIS_207_MAI IS NULL AND Y.CHAVE_MIS_207_ABR IS NOT NULL THEN 'UNIVERSO EXCLUSIVO ABRIL'
    END AS CONTROLO_UNIV
    ,CASE
        WHEN FLAG_ATIVO_PATRIMONIAL_ABR=1 OR FLAG_ATIVO_PATRIMONIAL_MAI=1 THEN (NVL(X.C457_MAI,0)+NVL(X.C460_MAI,0))-(NVL(Y.C457_ABR,0)+NVL(Y.C460_ABR,0))
    END AS GRSS_FEE_CMMSSN_INCM_MIS
    ,CASE
        WHEN FLAG_ATIVO_PATRIMONIAL_MAI=1 OR FLAG_ATIVO_PATRIMONIAL_LC_MAI=1 OR FLAG_ATIVO_PATRIMONIAL_CA_MAI=1 OR FLAG_ATIVO_PATRIMONIAL_ABR=1 OR FLAG_ATIVO_PATRIMONIAL_LC_ABR=1 OR FLAG_ATIVO_PATRIMONIAL_CA_ABR=1 THEN (NVL(X.C457_MAI,0)+NVL(X.C460_MAI,0))-(NVL(Y.C457_ABR,0)+NVL(Y.C460_ABR,0))
    END AS GRSS_FEE_CMMSSN_INCM_OFF_BLNC_MIS
    ,CASE
        WHEN FLAG_ATIVO_PATRIMONIAL_MAI=1 OR FLAG_ATIVO_PATRIMONIAL_LC_MAI=1 OR FLAG_ATIVO_PATRIMONIAL_CA_MAI=1 OR FLAG_ATIVO_PATRIMONIAL_ABR=1 OR FLAG_ATIVO_PATRIMONIAL_LC_ABR=1 OR FLAG_ATIVO_PATRIMONIAL_CA_ABR=1 THEN NVL(X.C298_MAI,0)-NVL(Y.C298_ABR,0)
    END AS GRSS_INTRST_INCM_MIS
    
FROM 
    (
    SELECT
	    CHAVE_MIS_207 AS CHAVE_MIS_207_MAI
	    ,FLAG_ATIVO AS FLAG_ATIVO_MAI
	    ,FLAG_ATIVO_PATRIMONIAL AS FLAG_ATIVO_PATRIMONIAL_MAI
	    ,FLAG_ATIVO_PATRIMONIAL_LC AS FLAG_ATIVO_PATRIMONIAL_LC_MAI
	    ,FLAG_ATIVO_PATRIMONIAL_CA AS FLAG_ATIVO_PATRIMONIAL_CA_MAI
	    ,FLAG_BUS_AREA AS FLAG_BUS_AREA_MAI
	    --,C001 AS C001_MAI
	    --,C007 AS C007_MAI
	    --,C134 AS C134_MAI
	    --,C224 AS C224_MAI
	    ,C298 AS C298_MAI
	    --,C299 AS C299_MAI
	    --,C300 AS C300_MAI
	    --,M009 AS M009_MAI
	    --,AC310 AS AC310_MAI
	    ,C457 AS C457_MAI
	    --,C458 AS C458_MAI
	    --,C459 AS C459_MAI
	    ,C460 AS C460_MAI
    FROM BU_CAPTOOLS_WORK.SIMUL_GD_MIS_MAI24
    WHERE (FLAG_ATIVO + FLAG_ATIVO_PATRIMONIAL + FLAG_ATIVO_PATRIMONIAL_LC + FLAG_ATIVO_PATRIMONIAL_CA)>=1
    )X
FULL JOIN 
    (
    SELECT 
	    CHAVE_MIS_207 AS CHAVE_MIS_207_ABR
	    ,FLAG_ATIVO AS FLAG_ATIVO_ABR
	    ,FLAG_ATIVO_PATRIMONIAL AS FLAG_ATIVO_PATRIMONIAL_ABR
	    ,FLAG_ATIVO_PATRIMONIAL_LC AS FLAG_ATIVO_PATRIMONIAL_LC_ABR
	    ,FLAG_ATIVO_PATRIMONIAL_CA AS FLAG_ATIVO_PATRIMONIAL_CA_ABR
	    ,FLAG_BUS_AREA AS FLAG_BUS_AREA_ABR
	    --,C001 AS C001_ABR
	    --,C007 AS C007_ABR
	    --,C134 AS C134_ABR
	    --,C224 AS C224_ABR
	    ,C298 AS C298_ABR
	    --,C299 AS C299_ABR
	    --,C300 AS C300_ABR
	    --,M009 AS M009_ABR
	    --,AC310 AS AC310_ABR
	    ,C457 AS C457_ABR
	    --,C458 AS C458_ABR
	    --,C459 AS C459_ABR
	    ,C460 AS C460_ABR
    FROM BU_CAPTOOLS_WORK.SIMUL_GD_MIS_ABR24
    WHERE (FLAG_ATIVO + FLAG_ATIVO_PATRIMONIAL + FLAG_ATIVO_PATRIMONIAL_LC + FLAG_ATIVO_PATRIMONIAL_CA)>=1
    )Y
ON X.CHAVE_MIS_207_MAI=Y.CHAVE_MIS_207_ABR 
    AND X.FLAG_BUS_AREA_MAI=Y.FLAG_BUS_AREA_ABR
    AND X.FLAG_ATIVO_MAI=Y.FLAG_ATIVO_ABR                                -- Comentário HF: Entre Abril e Maio năo identifiquei contratos a alterar esta flag de um męs para o outro 
    AND X.FLAG_ATIVO_PATRIMONIAL_MAI=Y.FLAG_ATIVO_PATRIMONIAL_ABR        -- Comentário HF: Entre Abril e Maio năo identifiquei contratos a alterar esta flag de um męs para o outro
    AND X.FLAG_ATIVO_PATRIMONIAL_LC_MAI=Y.FLAG_ATIVO_PATRIMONIAL_LC_ABR  -- Comentário HF: Entre Abril e Maio năo identifiquei contratos a alterar esta flag de um męs para o outro
    AND X.FLAG_ATIVO_PATRIMONIAL_CA_MAI=Y.FLAG_ATIVO_PATRIMONIAL_CA_ABR  -- Comentário HF: Entre Abril e Maio năo identifiquei contratos a alterar esta flag de um męs para o outro
ORDER BY CHAVE_MIS_207_MAI

