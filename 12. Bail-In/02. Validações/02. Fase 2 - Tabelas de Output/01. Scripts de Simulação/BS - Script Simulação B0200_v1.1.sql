	CAST(((LT005.OTSTNDNG_PRNCPL_AMNT_MOD - LT005.PRTN_AMNT_RPRTNG_ENTTY_MOD ) -1*LT005.ACCRD_INTRST_INSTRMNT_MOD) AS DECIMAL(21,2)) AS 0120_OTSTNDNG_AMNT, -- 28/10: WIP - Campo em desenvolvimento por IT
