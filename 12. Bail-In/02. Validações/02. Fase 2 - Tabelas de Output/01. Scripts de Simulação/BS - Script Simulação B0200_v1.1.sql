	'Not applicable' AS 0150_FLG_STRCTRLLY_SBRDNTD,
	CAST(LT002.RANK_INSOLV AS INT) AS 0160_RANK_INSOLV,
	999999999999999 AS 0161_CNTRCTL_SBRDNTN,
	LT002.RANK_INSOLV  AS 0162_BAIL_IN_CASCADE,
	LT002.DT_INCPTN AS 0170_DT_INCPTN,
	LT002.DT_NXT_INTRST_PYMNT  AS 0180_DT_NXT_INTRST_PYMNT, -- 28/10: WIP - Campo em desenvolvimento por IT
