	RT.NEW_ID_CORRIDA AS ID_CORRIDA,
	'${REF_DATE}' AS REF_DATE
FROM 
	-- Universo de tĒtulos  
	(
	SELECT *,
		CAST(ROW_NUMBER() OVER (ORDER BY INSTRMNT_ID ASC) AS INT) AS ROW_NUMB
	FROM
		(
		SELECT F1.*
		FROM 
			(
			SELECT DISTINCT INSTRMNT_ID
			FROM BU_LOANTAPE_WORK.LT002_INSTRUMENT
			WHERE REF_DATE = '${REF_DATE}'  
				AND AMBITO='SRB_MBDT' 
				AND SEGMENTO='ESTR'
				AND NOME_PERIMETRO= 'Individual Local'
				AND ENTIDADE='00100'
			)F1
		INNER JOIN 
			(
			SELECT DISTINCT CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT) AS CHAVE
			FROM BU_LOANTAPE_WORK.LT001_UNIV_OPER
			WHERE REF_DATE = '${REF_DATE}'  
				AND AMBITO='SRB_MBDT' 
				AND SEGMENTO='ESTR'
				AND NOME_PERIMETRO= 'Individual Local'
				AND ENTIDADE='00100'
				AND TRIM(FLAG_GAR_INTRG_SAN) <> '1' -- ExclusŃo do universo de Contratos/Responsabilidades associados Ós Garantias Pessoais - Operań§es constam na LT002 mas N├O constam no Reporte B02.00
			)F2 ON F1.INSTRMNT_ID=F2.CHAVE
		LEFT JOIN 
			(
			SELECT DISTINCT CONCAT(CEMPRESA,CBALCAO,CNUMECTA,ZDEPOSIT) AS CHAVE
			FROM BU_LOANTAPE_WORK.LT801_PARAM_SEGM
			WHERE REF_DATE = '${REF_DATE}'  
				AND AMBITO='SRB_MBDT' 
				AND SEGMENTO='ESTR'
			)F3 ON F1.INSTRMNT_ID=F3.CHAVE 
		WHERE F3.CHAVE IS NULL -- ExclusŃo do universo de Contratos/Responsabilidades dos Garantes que se pretendem incluir na lt003_entity - Operań§es constam na LT002 mas N├O constam no Reporte B02.00
		) AUX
    ) UNIV

LEFT JOIN 
	(
	SELECT *
	FROM BU_CAPTOOLS_WORK.SIMUL_LT002_INSTRUMENT
	WHERE REF_DATE = '${REF_DATE}'  
		AND ID_CORRIDA IN (SELECT MAX(ID_CORRIDA) FROM BU_CAPTOOLS_WORK.SIMUL_LT002_INSTRUMENT WHERE REF_DATE = '${REF_DATE}')
	) LT002 ON UNIV.INSTRMNT_ID=LT002.INSTRMNT_ID

LEFT JOIN 
	(
	SELECT INSTRMNT_ID,
		SUM(PRTN_AMNT_RPRTNG_ENTTY) AS PRTN_AMNT_RPRTNG_ENTTY_MOD,
		SUM(OTSTNDNG_PRNCPL_AMNT) AS OTSTNDNG_PRNCPL_AMNT_MOD,
		SUM(ACCRD_INTRST_INSTRMNT) AS ACCRD_INTRST_INSTRMNT_MOD
	FROM BU_CAPTOOLS_WORK.SIMUL_LT005_INSTR_ENT
	WHERE REF_DATE = '${REF_DATE}' 
		AND ID_CORRIDA IN (SELECT MAX(ID_CORRIDA) FROM BU_CAPTOOLS_WORK.SIMUL_LT005_INSTR_ENT WHERE REF_DATE = '${REF_DATE}')
	GROUP BY 1
	) LT005 ON UNIV.INSTRMNT_ID=LT005.INSTRMNT_ID
LEFT JOIN
	(
	SELECT NVL(MAX(ID_CORRIDA),0)+1 AS NEW_ID_CORRIDA
	FROM BU_CAPTOOLS_WORK.SIMUL_B0200
	WHERE REF_DATE = '${REF_DATE}'
	) RT ON 1=1

;

