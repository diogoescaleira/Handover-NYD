	999999999999999 AS 0300_AMNT_CVRD_ELGBL_DPSTS,
	'Unsecured' AS 0310_FLG_CLLTRL_AGRMNTS,
	999999999999999 AS 0320_AMNT_CLLTRL,
	999999999999999 AS 0330_AMNT_UNCLLTRLZD,
	'Not applicable' AS 0340_ID_CLLTRL,
	'Not applicable' AS 0350_TYP_CLLTRL,
	'Not applicable' AS 0351_TYP_CLLTRL_OTHR,
	LT002.TRDNG_MTHD AS 0360_TRDNG_MTHD, -- 28/10: WIP - Campo em desenvolvimento por IT
	CAST(LT002.AMNT_SCRTY_EUR AS DECIMAL(21,2)) AS 0370_AMNT_SCRTY_EUR,
